      <p><img src="stages/egg.gif" alt="Ceyrser's Egg Picture" width="109" height="90" align="right">The 
        winds pick up an icy chill as you walk to a further edge of the Eldora 
        Mountains. Cold air has swept across your face and burns it to the core. 
        Shivering, you duck down and follow the sunlight under a small ledge that 
        blocks the wind.</p>
      <p>The sun slightly warms you, at least much more than the evil wind did. 
        You take the time to stare up into the sky, soaking the sun in and dreaming 
        about loved ones back home...</p>
      <p>Leaning back to relax, you feel a strange bump keeping you from resting 
        comfortably. You look behind you, and there sitting in a make-shift little 
        nest of fur sits a grey egg.</p>
      <p>&quot;Hmmm...&quot; you wonder, &quot;I wish I knew what kind of egg 
        this was,&quot; for you have yet to see one in this world of its kind.</p>
      <p>The grey egg's shell glitters as you move away from it and the sun shines 
        it's heavenly beams upon it.</p>
      <p>&quot;I can only guess what grows inside it...&quot; you say as you tilt 
        your head slightly, trying once again to determine the species of dragon 
        that the egg may hatch. A smile widens on your face and you let the egg 
        be as you turn around and move on, slightly warmer than before.</p>
      <p align="center">* * *</p>
      <p>After a few days wandering through Eldora again, you come apon nest again. 
        The egg is no longer there! You search around the fur nest but still, 
        you can't find it...</p>
      <p>&quot;I hope no monster has stolen the egg... I really wanted to see 
        what hatched out of it!&quot;</p>
      <p>You sit down next to the nest and see a few broken piece of a grey shell... 
        maybe it hatched after all! You look further and dig around in the nest 
        a bit and see a small greyish snake like thing asleep burrowed underneath 
        the nest.</p>
      <p>&quot;Is this it? You couldn't have eaten that big of an egg, so you 
        must have been the one to hatch from it!&quot;</p>
      <p><img src="stages/baby.gif" width="215" height="148" alt="Ceyrser's Baby Picture" align="left">You 
        pick up the little grey creature, waking it up of course and hold it in 
        your palm. The baby starts to wriggle around a little bit, but mostly 
        stays put while making a few high pitched calls in your direction.</p>
      <p>&quot;What a lively fellow...&quot;</p>
      <p>You place the baby back in the nest and cover it up with the fur.</p>
      <p>&quot;I wonder if I should stay to watch this baby grow...&quot;</p>
      <p>You look through your traveling pack and realize that you haven't much 
        food left.</p>
      <p>&quot;Doh, I should have picked some berries when I had the chance...&quot;</p>
      <p>You leave the nest hoping to come back another day to see it grown.</p>
      <p align="center">* * *</p>
      <p>You take the northern route around Eldora and visit a few more places 
        with interesting animals, yet remember about the little creature that 
        you burried back in that nest... You travel southward again hoping to 
        find that nest again, and this time you have pleanty enough food to wait 
        out the growing stage. ^_~</p>
      <p>You come upon that small ledge again, expecting to see a small nest, 
        but this time you see a large, platinum colored dragon sleeping inside!</p>
      <p align="center"><img src="stages/ceyrser.gif" width="749" height="461" alt="Ceyrser"></p>
      <p>&quot;Aww... where did the little baby go?&quot;</p>
      <p>Finally realizing that THIS must be that little baby, you creep up next 
        to the dragon and sit next to it. The animal lifts its head and sees you... 
        automatically she wraps her tail around your body tightly.</p>
      <p>&quot;Oh mother! Where have you been??&quot;</p>
      <p>&quot;What??&quot;</p>
      <p>&quot;Mommy... you left me in the nest and went away!&quot; ;.;</p>
      <p>&quot;I am not your mother...&quot;</p>
      <p>&quot;You aren't? But you spent so much time with me! Who else would 
        sit by my nest and wait for me to come out?&quot;</p>
      <p>You think for a minute and deduce that you have been a sort of 'mother' 
        figure...</p>
      <p>&quot;I am not your biological mother, but yes, I waited to see you come 
        out of your egg, and grow up.&quot; ^_^</p>
      <p>&quot;I can't believe you left, I don't even have a name...&quot;</p>
      <p>&quot;Aww, poor little dragon... how about I give you a name?&quot;</p>
      <p>She pouts for a second, &quot;I am not little anymore, mommy. I am a 
        full grown flion!&quot;</p>
      <p>&quot;Oh, so that's what you are... Well, we need to give you a strong 
        name... how about Ceyrser?&quot;</p>
      <p>&quot;Ceyrser... I like it. ^_^ What does it mean?&quot;</p>
      <p>&quot;I heard a Hearthian use it once, they said it meant strength in 
        friendship.&quot;</p>
      <p>&quot;Am I your friend...?&quot;</p>
      <p>&quot;Yes, Ceyrser, you are very special to me.&quot;</p>
