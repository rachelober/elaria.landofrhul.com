      <h2>Flight</h2>
      <p>Iada, a young Seque of inner Eldora, woke up early while the sun sat 
        still on the western horizon. It was early spring in Eldora, and most 
        people preferred warm beds over the chilled wind in the morning. Unlike 
        the others, Iada loved to feel the piercing breeze brush the separate 
        feathers of her wings and rustle the individual dark brown hairs of her 
        thick fur.</p>
      <p>Seque's are known for their ability to fly to high altitudes in Eldora 
        and their graceful landing. To humans, Seques resemble large cats with 
        wings. The species stands upright like homo sapiens, but live with a lighter, 
        more hollow frame which makes them capable for flight. Therefore, the 
        species must be extremely careful of their landings to not break their 
        light bones.</p>
      <p>The young female leans over her bed and places her feet on the cold ground. 
        Her mother, father, and younger brother must still be asleep as she hears 
        the slight snoring of her father off in the other corner of the complex. 
        While shaking her head with a smile, she grabbed a pair of pants after 
        checking herself in the mirror. Iada had bright blue eyes that shined 
        whenever she smiled and short thick fur that covered the entity of her 
        body, down to the pads of her feet.</p>
      <p>With long, quiet strides, Iada crept down the sleep stairs from the second 
        floor of her family's complex to the first. Her parent's did not approve 
        of her creeping away when they didn't know where she was going. &quot;They 
        won't even know I left,&quot; she muttered to herself as she calmly waltzed 
        out the front door onto the sidewalk and made her way to outside of Auste, 
        the central city of Eldora.</p>
      <p>Past all of the bright lights and mucky smell of the city, the countryside 
        of the mountains grew clearer and the sky beamed with bright blue. By 
        the time she made it across the first bridge way to the second concentric 
        circle outside of Auste, the sun had risen a bit, and she could already 
        smell the flowers.</p>
      <p>Almost every morning, Iada would wake, travel to this spot and practice 
        her &quot;dare-devil&quot; flying, as her father put it- obviously he 
        and her mother never approved of this hobby either. Being a well respected 
        family in Auste, Iada too was expected to fulfil the deeds of her relatives 
        and become a well-rounded lady of the city; well rounded in the sense 
        that she wore dresses and pretty earrings and wings were strictly detrimental.</p>
      <p>At an early age, her cousin taught her how to actually use those appendages 
        on her shoulder blades.</p>
      <p>&quot;Now, I want you to jump off that ledge right now, Iada!&quot;</p>
      <p>&quot;No! I am going to kill myself, I should have never listened to 
        you!&quot;</p>
      <p>Her cousin stared at her sternly. &quot;Are you telling me you rather 
        sit all day inside, drinking cups of tea, instead of doing this?&quot;</p>
      <p>Iada paused for a moment and replied with a whisper, &quot;No... I don't.&quot;</p>
      <p>&quot;Then listen to me!&quot;</p>
      <p>Iada bent her head down and turned away from her cousin to face the cliff. 
        With a strong breath, she bounded down the makeshift runway and jumped 
        into the thin air. The ground left her feet and her wings grabbed the 
        delicate strings of air that held her tight as her wings beat in rhythm. 
        She never doubted again.</p>
      <p align="center"><img src="images/hr.gif" alt="" width="400" height="5"></p>
      <h2>Accident</h2>
      <p>Today, she wanted to work on high altitudes of flight, nosedives and 
        recovering from the drop. She never worked like this before, but was determined 
        to do it.</p>
      <p>Stretching, Iada prepared her mind for the flight. "Let the wind grab 
        your wings... let the wind grab your wings." </p>
      <p>Unluckily for her, the winds had grown extremely turbulent. She assumed 
        a storm front was moving in from the north, which brought the cold air. 
        She shook the feeling off and concentrated on the altitude. </p>
      <p>Confident, Iada stood against the wind and walked to the edge of the 
        cliff. By now, she had grown stronger since her first flight and could 
        nearly drop from the edge and catch the wind, as a net catches a falling 
        object, yet lifts it up. Her wings opened to receive the breeze then descended.</p>
      <p>The winds caught her almost immediately and Iada began her paddle up 
        to the highest cloud she could see. The higher she went, the clouds grew 
        terrible and the wind ruptured with a force unimaginable; the storm had 
        moved in quicker than she expected and her body tossed back and forth 
        between the obscured mists.</p>
      <p>With a scream, Iada falls from the high clouds; the nosedive she wanted 
        to accomplish fails her to a terrifying, uncontrollable fall from the 
        heavens. Desperately trying to gain the wind's power again, her wings 
        disobey and fold to her back. As the ground draws near, Iada closes her 
        eyes and feels the worst pain in her life. </p>
      <p align="center">* * *</p>
      <p><img src="stages/egg.gif" alt="Egg" width="109" height="90" align="right">In 
        an unimaginable amount of time, Iada weakly pried her right eye open. 
        Her left arms stretched in front of her slain body as her right arm tucked 
        under her body. Unable to feel her legs, she breathes heavily and catches 
        a glimmer from a stone slightly to her left. The gray object glitters 
        in the sunlight- the storm understandably has passed- and Iada extends 
        her fingers to clasp the oval object. And her eyes close again. 
      <p align="center"><img src="images/hr.gif" alt="" width="400" height="5"> 
      <h2>Asdeno</h2>
      <p>&quot;I think she will recover fine Dek, her bones will heal quickly, 
        but she might not be able to fly again. I am more worried about the cuts 
        and hit she took on her head.&quot;</p>
      <p>&quot;Thank you Doctor Medes for coming all this way.&quot;</p>
      <p>&quot;It wasn't any trouble, I just hate to see Iada like this. She was 
        always one to go against the norm.&quot;</p>
      <p>&quot;I know. She must be more careful.&quot;</p>
      <p>Iada weakly opened her eyes at slits and watched as her father talked 
        to Doctor Medes. Her body ached all over and her chest felt as if her 
        breath had been pushed out by force.</p>
      <p>&quot;Well, take care Dek, I will come by tomorrow to see how she is 
        doing. Make sure she stays in bed, and when she wakes up, to keep her 
        calm and comfortable.&quot;</p>
      <p>&quot;Will do!&quot;</p>
      <p>Doctor Medes steps outside and she can faintly hear his footsteps down 
        the stairs. Her father retreated to Iada's bedside and whispered. &quot;Dear,&quot; 
        a sigh escaped his crumbling lips as he continued, &quot;We were so worried, 
        your brother set out to find you when we went to wake you up this morning 
        and your bed sheets were cold. He knew where to find you and miraculously 
        carried you home.&quot; He paused to swallow and continued to speak, &quot;Doctor 
        Medes says that both of your legs are broken, your left wrist is twisted 
        badly, your head has been banged up pretty badly and, &quot; Dek took 
        a breath, &quot;your wings are all torn up, dear.&quot; He swallowed again.</p>
      <p>Iada grunted a bit and opened her eyes fully to witness her father's 
        wrinkled facial features.</p>
      <p>&quot;Good Morning Iada. How are you feeling?&quot;</p>
      <p>She gasped slightly in order to speak, &quot;Like I am tied to the floor 
        by 5 tons of bricks.&quot;</p>
      <p>Dek smiled haplessly and cocked his head, &quot;Still have your sense 
        of humor, at least that was kept.&quot;</p>
      <p>&quot;What, what happened?&quot;</p>
      <p>&quot;Your brother found you beaten up over outside of town by the countryside. 
        You can explain to us later what happened but right now, I should leave 
        you to rest. Your mother and brother are all very worried, I should console 
        them a bit. if you need anything, just give us a ring, &quot; pointing 
        to a small silver bell by her bedside, there she noticed the gray object 
        she had picked up after she fell.</p>
      <p>Her father left the room as Iada pretended to fall back asleep long enough. 
        When his footsteps faded down the stairwell, she grabbed an extra breath 
        and leaned closer to her nightstand. There she could see the object more 
        clearly. Because of her left hand being sprained, she attempted to lean 
        to the table on her left while reaching out with her right hand to get 
        a better look at the oval object.</p>
      <p>She snagged it quickly and brought it back to her lap as she promptly 
        propped herself up on a few pillows, withstanding the pain in her abdomen.</p>
      <p>After a quick inspection, Iada realized that it wasn't a stone, but a 
        shiny egg that she had found. A grin spread across her lonely face, the 
        egg was warm to the touch and soft on her fingers. Dreariness fell over 
        her head once more as she tucked the egg under her covers and delighted 
        herself with a little nap.</p>
      <p align="center">* * *</p>
      <p>Iada yawned as she opened her eyes once more to the day. It was later, 
        but she didn't think she was asleep for more than maybe an hour- at most. 
        She shrugged to herself inside; she didn't even know how long she had 
        been asleep since her brother had found her and when she woke up as Doctor 
        Medes left her room.</p>
      <p>The egg! She remembered the egg, she desperately dug through her sheets, 
        attempting not to strain herself too much, but finally snatched the egg 
        underneath her quilt.</p>
      <p>She rolled the egg in her palm to take a closer inspection of it. Along 
        one of the sides, a long slender crack crept along the diameter. Her face 
        dropped at the sight of it, she must have rolled over on it accidentally 
        while she slept. She began to wonder what to do with the object since 
        she was sure she had hurt it forever when a crack came from the egg.</p>
      <p>Over the next hour, she watched in anticipation as a skinny gray serpentine 
        creature crept from the innards of the shell and fell into a tiny ball 
        in Iada's lap. The creature's eyes opened almost immediately and stared 
        at its new mother.</p>
      <p align="center"><img src="stages/baby.gif" alt="Asdeno" width="215" height="148"></p>
      <p>Iada grinned and held the little one up to the light. &quot;So you are 
        the one that has been hiding in that shell all this time! And here I thought 
        it might have been a bird!&quot; She was slightly disappointed that the 
        creature didn't have wings, maybe one day they could go flying together; 
        but the shock had not hit her yet of just how beat up she was, she didn't 
        even think of never being able to fly, and forgot distinctly what the 
        Doctor had said.</p>
      <p>&quot;What should I name you, little gray one?&quot; The serpent rolled 
        around in her hands, like it had when it still resided within the shell. 
        &quot;I don't even know if you are a boy or a little girl!&quot; She nuzzled 
        the pet and set it down on her sheets. &quot;Ok then, I will name you 
        Asdeno, or 'Without fear'. I feel we will have great adventures, Asdeno!&quot; 
        She grinned at the baby and snatched it up into her chest to lay to sleep 
        once again.</p>
      <p align="center"><img src="images/hr.gif" alt="" width="400" height="5"></p>
      <h2>The Bigger Problem</h2>
      <p>Months passed before Iada could walk again alone. Her legs had been badly 
        broken in the fall and her father remained even more protective of his 
        only daughter. Her mother, she remembered, just looked forlornly at her 
        daughter. Never had she seen such damage to a young soul. Iada recalls 
        her mother speak to her little brother, &quot;Now remember this when YOU 
        want to be rebellious!&quot; Why did she always make Iada's problems bigger 
        than they really were?</p>
      <p>There was one time Iada thought she was going to get a bad grade on her 
        test- even before taking it- and her mother grounded her to her room for 
        the weekend. That only happened a year or so ago, Iada soon learned that 
        she couldn't tell her parents much of anything.</p>
      <p>&quot;Iada? Why'd you go so far out?&quot; Giri, her brother crawled 
        up to her chair that sat on the edge of her room situated to look outside 
        but only gave a pitiful view of the street below.</p>
      <p>&quot;Huh? What are you talking about Giri?&quot; Iada said and motioned 
        her body to let Giri sit on the seat with her.</p>
      <p>&quot;You hurt yourself.&quot; His face dropped and he shifted his eyes 
        to Iada's torn wings and gave the impression that he wanted to reach out 
        and pull one of the feathers.</p>
      <p>Iada sighed, &quot;I told you, I'm a teenager, Giri. I go out and do 
        &quot;rebellious&quot; things!&quot;</p>
      <p>&quot;That's just what mum said, I know you think differently.&quot;</p>
      <p>Iada was astonished at Giri's precocious thoughts. She cocked her head 
        slightly and narrowed her eyes, &quot;Well, you've been outdoors. Isn't 
        it different from the city?&quot;</p>
      <p>Giri nodded and grinned a bit.</p>
      <p>&quot;I thought so.&quot; She lifted her arm and pointed at all the tall 
        buildings. &quot;That, outside, isn't flying space.&quot;</p>
      <p>&quot;Obviously,&quot; Giri chuckled.</p>
      <p>&quot;Right. So where would you go to fly?&quot;</p>
      <p>&quot;Fly?&quot; Giri cocked an eyebrow. &quot;We don't fly.&quot;</p>
      <p>Iada closed her eyes and sighed, &quot;Then why do you think we have 
        wings, Giri?&quot;</p>
      <p>He opened his mouth but paused, &quot;... I don't... know.&quot;</p>
      <p>&quot;Right, that's because no one understands or knows of our past anymore. 
        Wings were meant to fly. Therefore, everyone was meant to fly. It is the 
        same as not using your hands to feed yourself, it is just another tool 
        for us Seques that Aille gave us.&quot; She opened her eyes again and 
        smiled at Giri, whom seemed a little weary.</p>
      <p>Giri held back a comment, she could tell, but he began to speak &quot;Mommy 
        said you were filled with Dreche,&quot; and faded off towards the end.</p>
      <p>&quot;Ugh!&quot; Iada groaned, &quot;What an insult!&quot; Iada continued 
        to growl when her mother walked in the room.</p>
      <p>&quot;Giri?&quot; she peaked her head in to catch Giri's attention.</p>
      <p>&quot;Yes, Mommy?&quot;</p>
      <p>&quot;It is time for dinner now.&quot;</p>
      <p>&quot;Ok Mommy.&quot; Giri hopped down from the seat and scuttled off 
        down the stairs.</p>
      <p>Iada's mother turned to leave again, but halted and quickly faced her. 
        &quot;You know, it would be appreciated if you would join us for dinner 
        once and a while.&quot; She clenched her hands by her stomach.</p>
      <p>Iada turned away and stared at the window, even though nothing in particular. 
        &quot;If I felt welcome.&quot;</p>
      <p>&quot;Iada, dear...&quot;</p>
      <p>&quot;No Mum,&quot; she whipped around in the chair, &quot;I know what 
        you really think I am, and I can't take you anymore.&quot;</p>
      <p>Iada's mother didn't say another word. This type of argument went on 
        now since Iada could walk. Iada would sit in her room all evening and 
        then wobble down the stairs at some ungodly hour only to eat by herself.</p>
      <p>When Iada's mother left the room, Iada clenched her fists on the seat 
        and grabbed the tight white material. &quot;Why?&quot; she whispered through 
        blistering tears.</p>
      <p align="center">* * *</p>
      <p>After a while, Iada got up as she did every night for the past two months 
        and walked to her closet. There inside was her little secret, Asdeno.</p>
      <p>She shuffled over and opened the door.</p>
      <p><img src="stages/asdeno.gif" alt="Adeno" width="741" height="458"></p>
      <p>Well, maybe he wasn't such a little secret anymore.</p>
