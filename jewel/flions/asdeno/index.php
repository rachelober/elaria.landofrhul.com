      <h2>Return to the Skies</h2>
      <p>Asdeno had grown substantially since his birth, obviously. He could hardly 
        rest soundly in the closet, although it was very big, and would constantly 
        hit his head on the roof of the tiny room.</p>
      <p>Iada laughed at this since she paled in size to him. &quot;Asdeno, are 
        you ready to go downstairs?&quot; It was later and her parents had already 
        gone to bed. The flion nodded his head and gave a little grin. &quot;Ok, 
        then you have to be QUIET this time,&quot; she warned.</p>
      <p>Last night, he was almost spoiled when he hit his head on the hallway 
        chandelier and burst one of the light bulbs. Asdeno flew down the stair 
        well and resided downstairs as her parents clamored out of their bedroom 
        door and questioned the &quot;sleepy&quot; Iada what happened.</p>
      <p>The dragon slithered out the door of Iada's room and carefully ducked 
        his head around each object and corner. He was doing very well this time. 
        He slithered quickly down the stairs and huddled at the bottom until Iada 
        was able to make it all the way down. While taking each step carefully, 
        she gazed out the window in her hallway and could see the city's lights. 
        For the first time she could gaze out and almost think that they were 
        pretty.</p>
      <p>She soon got to the floor and patted Asdeno, &quot;Thank you for waiting,&quot; 
        and entered the kitchen.</p>
      <p align="center">* * *</p>
      <p>She sighed a bit, &quot;Asdeno, I don't think there is anything in here 
        you can eat!&quot; She hurriedly shuffled through the shelves and couldn't 
        find a single thing that would satisfy Asdeno's humongous appetite.</p>
      <p>At this cry of despair, Asdeno flung Iada on his back and began to slither 
        at a high pace. &quot;Wait! Asdeno what are you doing?!&quot; she cried. 
        He grunted a bit but gathered speed down the hallway and lowered his head. 
        &quot;No!&quot; Iada cried once more before Asdeno's horns crashed into 
        the wall and through the other side like a knife through butter.</p>
      <p>Asdeno slithered for a few more meters but quickly opened his wings and 
        lifted from the ground. Iada kept her eyes shut until Asdeno reached maximum 
        speed.</p>
      <p>&quot;Asdeno...&quot; she whispered into his ear, &quot;since when have 
        you done this?&quot;</p>
      <p>&quot;This is my first time,&quot; a deep voice echoed. Iada was surprised 
        to receive an answer, never before had he talked to Iada before. &quot;I 
        thought it was about time I took you back.&quot;</p>
      <p>&quot;Took me back?&quot;</p>
      <p>Asdeno flew over the bridge to the other mountain and dropped from the 
        sky like a white shooting star. He gracefully landed on the ground and 
        coiled up so Iada could slide down his body.</p>
      <p>&quot;You can't stay bitter forever, mother.&quot;</p>
      <p>&quot;You called me mother...&quot; she rubbed Asdeno's leopard print 
        skin and looked into the sky. Out here the city lights couldn't burn any 
        star.</p>
      <p>&quot;You are just that to me.&quot; He shuffled his wings and uncoiled 
        his body. &quot;Everyday now, I listen to you talk to me, but I also hear 
        you argue with your family. I know you've wanted to fly again, it is in 
        your voice when you speak and your tears when you cry at night.&quot;</p>
      <p>He opened his wings wide. &quot;I had no idea,&quot; she replied. She 
        climbed on his body again and they lifted into the air. She opened her 
        own wings and felt the cool night air hug every fiber of the plumes, a 
        feeling she missed greatly.</p>
      <p>Iada closed her eyes and let herself fly free. Her arms let go of Asdeno's 
        back and her body opened to the air. Her wings caught the breeze and and 
        floated endlessly. Soon her wings picked up the rhythm again, he heart 
        pounded and she opened her eyes again. There in front of her she could 
        see an endless night sky glitter. Asdeno had flown ahead of her and waited 
        for her to catch up. She flapped her wings and continued to fly.</p>
