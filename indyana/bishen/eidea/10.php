    <h1>Demintrix</h1>
    <p>Ad'cero awoke on the beach of the northern corner of Demintrix, but would 
      only find this out later. Mostly wet still, she could see Im'os nearby. 
      Her body was weak, but being born of a Hearthan, she could move mountains. 
      She steadied her arms on the wet sand and lifted her body. She audibly cracked 
      her back in a few places as she stretched, but otherwise was fine, mind 
      a few cuts and bruises.</p>
    <p>Flipping her body over so she could sit upright, she took a few breaths 
      and stared at the horizon. It was near night and she could already feel 
      the cool breeze float along the sea's top. Im'os was breathing, she could 
      tell from where she sat because of his chest leaping from time to time to 
      catch some air.</p>
    <p>After a few days in bed resting, Iniara was strong enough to walk around 
      the room she stayed in. Derik had been very sweet and delivered her tea 
      in the mornings and would open the blinds to her room and let her see the 
      sun rise early over the horizon. He would stay with her all day, unless 
      she napped in the afternoon and learned more about this strange woman that 
      happened to appear on his land.</p>
    <p><q>How old are you, Nara?</q> Derik asked her on a day.</p>
    <p><q>Oh,</q> she paused and gazed down at her little china cup that contained 
      some vanilla tea, <q>I think about 224 seasons. I haven't been keeping track, 
      and I wasn't sure of my birth... so...</q> she stopped there and returned 
      her gaze to Derik, <q>It never bothered me much exactly how old I was.</q></p>
    <p>Merold walked into Iniara's bedroom and asked about breakfast, <q>Oh, we 
      have some nice eggs this morning, would you like that for breakfast this 
      morning?</q></p>
    <p>Iniara was ready to reply, but a nerve struck her hard <q>Eidea'col!!</q></p>
    <p><q>What, Nara?</q> Derik responded urgently.</p>
    <p><q>My, my clothes, where are they?</q></p>
    <p><q>They are torn and dirty, why would you want them back?</q></p>
    <p><q>Let me see them! Bring them here to my bed! Immediately!</q> she snarled.</p>
    <p>Derik rushed out of the room and returned a moment later with Iniara's 
      robe. He laid it on the bed in front of her and she frantically dug through 
      her pockets.</p>
    <p><q>No!</q> she cried, <q>How could I have let this happen! Derik! When 
      you found me, did you see a black egg by chance?</q> she asked desperately.</p>
    <p>Worried and perplexed, Derik replied, <q>No, Nara. I did not see a thing 
      but you...</q></p>
    <p>Her eyes filled with tears, something she had not felt in a long time. 
      <q>Derik...</q> she whimpered. <q>Please, I must find Eidea'col and soon. 
      I'm afraid she could be out there all alone.</q></p>
    <p><q>Are you well enough?</q></p>
    <p><q>Yes, I must find her!</q> she pleaded.</p>
    <p><q>Ok, I'll have my mother help you get dressed for the outside. But you 
      have to take it easy. I don't want to see you bed ridden for another week, 
      eh?</q></p>
    <p>She brightened up slightly and hope filled her eyes as she nodded. Derik 
      had a way with words to make the situation seem all right. Merold knocked 
      at her door after Derik left and brought in a lovely violet robe with silver 
      stitching. <q>I thought this might fancy your taste, Nara,</q> she spoke 
      humbly and helped Iniara out of bed and sat her on a stool and began to 
      dress her. The robe fit wonderfully around her figure and hung loosely around 
      her lower arms but tight around her waist.</p>
    <p>Merold then took her to a mirror and began to brush out Iniara's long silver 
      hair. <q>Oh, I use to have long hair just like this as a girl, Nara.</q></p>
    <p>Iniara blushed; she always prepared her hair by herself in the past but 
      allowed Merold to fix it for her. Merold painstakingly braided a few strands 
      so that it would hang around Iniara's face. She also trimmed the edges of 
      her hair so that it was all one length. To finish the style, she added a 
      few daises to the top of each braid and scattered along individual strands.</p>
    <p><q>This is an ancient style of hair decoration,</q> Merold went on to explain. 
      <q>On special celebrations or festivities, Daemenites would braid their 
      hair and stick newly bloomed flowers within the ties.</q></p>
    <p><q>Daemenites?</q> Iniara exclaimed.</p>
    <p><q>Mm... yes, dear. That is what we are after all.</q></p>
    <p><q>No,</q> she asserted, <q>You must be mistaken. Daemenites are evil creatures 
      and powerless mongrels.</q></p>
    <p><q>Well, I never heard such a distasteful expression of someone's heritage!</q></p>
    <p><q>Heritage? I have no family!</q> Iniara cried and stood.</p>
    <p><q>Look at your body, Nara. You are the same as me. You are a Daemenite.</q></p>
    <p><q>No, I have ice skin and cold eyes; never would I fall to abominations!</q> 
      Iniara stood and faced the mirror, and in surprise noticed her skin a darker 
      teal and her eyes a calm blue. <q>What have you done to me?</q></p>
    <p>What Iniara did not understand was the essence of the Daemenites, although 
      magic, consisted of the growth of plants. Conception of Daemenites is helped 
      by different plant structures. These plants become one with them; chlorophyll 
      being one aspect of a plant, when Daemenites are in the sun for periods 
      of time their skin turns to a natural, healthy deep teal. From her days 
      out on escapade and laid out on the beach, she must have developed what 
      human's would know as a <q>tan</q>.</p>
    <p><q>Nara?</q> Derik yelled from outside as he opened the door. <q>Are you 
      ok? Mother, what happened?</q></p>
    <p><q>I don't understand, son. She does not want to accept that she is a Daemenite.</q></p>
    <p><q>Ah! Don't say that Merold!</q> Iniara wailed.</p>
    <p><q>Nara, let me take you outside, maybe being locked inside has done some 
      adverse affects on your health.</q> Derik quickly led her outside and ushered 
      her along the sandy path to the beach.</p>
    <p><q>By Aille, what happened Nara?</q> Derik questioned.</p>
    <p><q>I can't think, I must find my Eidea'col!</q> Iniara whimpered though 
      tears.</p>
    <p><q>Shh, shh...</q> Derik brushed away the falling tears from her cheeks. 
      She sniffled and eventually calmed herself. <q>There now, Nara. Now we can 
      search for your egg since you are calm again.</q> He made a silly grin that 
      made Iniara chuckle.</p>
    <p><q>A chuckle?</q> Iniara thought. What has happened to me?</p>
    <p>Derik led her down to the beach by the arm. <q>So, what is this egg, Nara?</q></p>
    <p>She waited to gather her thoughts then responded, <q>A child for me to 
      take care of,</q> she smiled.</p>
    <p><q>A child?</q></p>
    <p><q>Well, actually a Bishel Dragon, or a soon to be dragon I suppose. If 
      I ever find the egg...</q> She sighed and continued, <q>I traveled to the 
      Bishen Realm to gather this egg to foster, and it would be a shame to lose 
      it after all this.</q></p>
    <p><q>True, but what did you have to do to gain trust and gather this egg?</q></p>
    <p><q>I had to prove my worth. The leader of the clan I fostered from was 
      a real tough character. It didn't seem like he wanted me to have the egg 
      at all.</q></p>
    <p><q>I can't believe that,</q> Derik stopped and turned Iniara to face him, 
      <q>from what I see in you, you would be an excellent mother.</q></p>
    <p>She flushed at his comment. Only days before she would have given his word 
      a second thought. <q>I would like to believe that, Derik.</q></p>
    <p>They made their way down to the beach and Derik showed her where he found 
      her. <q>It was later in the day and the tide was out. You were laying on 
      your back and beaten up. Heh,</q> he stopped, <q>I couldn't believe my eyes 
      when I saw you.</q></p>
    <p>Iniara bent her head and didn't say anything, but she smiled inside. Derik 
      began to speak again but a loud cracking noise came from nearby.</p>
    <p><q>What was that?</q> Iniara exclaimed.</p>
    <p><q>I don't know... it sounded like something broke...</q></p>
    <p>The two moved to a little patch of sea grass and heard the sound again. 
      <q>There! I heard it again!</q> Brushing away some of the reeds Iniara whimpered, 
      <q>Eidea'col! My egg... it's broke!</q></p>
    <p><q>Are you sure it is your egg?</q></p>
    <p><q>Positive,</q> she wept, <q>the same exact pattern and everything-</q></p>
    <p><q>Wait,</q> Derik interrupted, <q>I don't think she's dead, I think she 
      is hatching!</q></p>
    <p>The two stared as a claw stretched out of the hole in the egg. Iniara gripped 
      at Derik's sleeve as she witnessed Eidea'col's birth. First, two clawed 
      feet punctured the edge and wriggled out. After a momentary rest, Eidea'col 
      began to move again and a dark tail whipped itself out and broke more of 
      the shell. A large crack grew along one side and a clawed wing soon followed 
      to break out. Soon, Eidea'col poked her tightly closed eyes out of the hole 
      and used her horns to pry open the container more. She stopped for a little 
      while and took a few breaths.</p>
    <p><q>C'mon Eidea'col, you can do it! Break that shell!</q></p>
    <p><img src="stages/wyrmd.gif" alt="Eidea'col" width="168" height="189" align="left">Eidea'col 
      soon started again and finally released her second wing and began to pull 
      herself from the shell and out into the air. Iniara ran to her side. <q>Eidea'col! 
      I've found you!</q> Iniara picked her up and held her to the sky. Eidea'col 
      easily fit into her palm and rested there soundlessly. <q>Look,</q> Iniara 
      spoke as she held Eidea'col up, <q>She has the color of your hair,</q> she 
      giggled.</p>
    <p><q>Wow, I've never seen such a heavily armored tail before...</q></p>
    <p><q>Yes... I can already tell she will be a fighter.</q> The coldness in 
      Iniara crept out of her for a time being but Derik quickly grabbed her and 
      spun her around.</p>
    <p><q>What a happy day! And with the Moon Festival happening in a few days, 
      you can show her off to everyone!</q></p>
    <p><q>Everyone?</q> gulped Iniara. She hardly felt like meeting new people. 
      She had been so use to her court in Litor that she forgot how to be hospitable, 
      if she had been hospitable ever in her life.</p>
    <p><q>Oh yes, the entire tribe will be attending. There will be so much food 
      and games and music- a truly fun time!</q></p>
    <p>Iniara nodded, <q>Ah... I see...</q> she stopped speaking and gazed along 
      the horizon. After a long while she asked, <q>Derik?</q></p>
    <p><q>Yes?</q></p>
    <p><q>I never found out your age...</q></p>
    <p><q>Oh, I suppose around my 206th season.</q></p>
    <p><q>Ah,</q> she acknowledged, <q>How is it that you have lived so long?</q></p>
    <p><q>Long? I'm still merely a child to a Daemenite.</q></p>
    <p>Iniara stared blankly out into the sea, <q>A child?</q></p>
    <p><q>Yes, Daemenites usually live to 700 seasons.</q></p>
    <p>She gulped hard and crumbled at her knees to the ground, <q>700...?</q></p>
    <p><q>Yes, some more!</q></p>
    <p>Her eyebrows quivered and her lips did not stay steady. <q>A child... only 
      but a child.</q></p>
    <p>Derik brought her and Eidea'col back to his home that day and Iniara began 
      to use her motherly instincts to care for the little wyrm. She saw that 
      Merold coward when she entered the house again. Usually a common action 
      when she entered any hall in Litor, she didn't quite expect it from her, 
      and for once in her life felt that she needed to apologize.</p>
    <p><q>Merold,</q> Iniara commented softly, <q>I am,</q> she paused and looked 
      to Eidea'col who had crumpled into a little ball in her hands and fell asleep, 
      <q>I am sorry for the way I acted earlier. I did not mean to attack you 
      the way I did.</q></p>
    <p><q>Say nothing more,</q> she assured, <q>You have been through a lot,</q> 
      she reached out and lightly held Iniara's arm, <q>We have a festival to 
      plan for now.</q> She gazed lovingly down at Eidea'col and lightly stroked 
      the back of her spine with the tip of her index finger.</p>
    <p><q>What is this festival that you speak of?</q></p>
    <p><q>Ah,</q> Merold acknowledged, <q>The greatest of them all. This is when 
      the moon makes its travel in the highest part of the sky. If the sky is 
      clear, it predicts health and happiness in the new year.</q> Iniara continued 
      to nod, trying to understand its importance. <q>Heh, Nara, no need to nod 
      your head off! I can see you don't care much. No matter, you will meet our 
      tribe and become one of family.</q> She smiled at Iniara warmly and glanced 
      at Derik who was standing, nervously, by.</p>
    <p><q>Family?</q> Iniara stared down at Eidea'col, who still seemed pretty 
      tired from her cracking of the egg. <q>Hmm, You don't seem like the little 
      demon I expected you to be, heheh,</q> she thought. </p>
    <p><q>Nara?</q> Derik called, <q>Can you come over here please?</q></p>
    <p>Iniara nodded and placed Eidea'col in a little basket Merold had laying 
      around. <q>What is it that you wanted, Derik?</q></p>
    <p><q>Do you remember whenever you first told us your name, Iniara, right?</q></p>
    <p><q>Yes, I remember everything so well since I've been with you,</q> she 
      smiled but greeted a serious glare from Derik.</p>
    <p><q>You can't tell anyone that is your real name.</q></p>
    <p><q>But why? Is this why you call me Nara?</q></p>
    <p>Derik hesitated, <q>Yes, in the simplest answer. About 250 seasons ago, 
      a strange prediction was concocted. <q>A child of ice will be born under 
      the festival moon from ivy. Her impish king will stow her away and 200 seasons 
      later return her to her home, which will bring upon the end of the Daemenite 
      reign.</q> Ah, not words for word, but you get the idea, eh?</q></p>
    <p><q>Wait, what are you trying to tell me?</q></p>
    <p><q>What was said to happen was that someone was to steal the child during 
      the Moon Festival, and one year,</q> he stopped and scanned the room, Merold 
      had luckily walked away and resumed with his voice in a whisper. <q>One 
      year a terrible devil emerged and killed a great deal of people. Legend 
      says he must have known about the powers Daemenites consist of when born 
      under a full moon, and he wanted a child to manipulate to his own. That's 
      my only deduction.</q></p>
    <p><q>No one found the girl named... Iniara. Many did not want to believe 
      that a child had been stolen, it had never happened before, but no great 
      evil had ever plagued the Daemenites without the people being able to control 
      it. Seemingly embarrassed, they counted her among those dead.</q> He bowed 
      his head, <q>Iniara means <q>ice girl</q>. The child had been born with 
      elegant blue skin. Can you see where this is going, Nara? I'm afraid you'll 
      be attacked if someone finds out your real name.</q></p>
    <p>Iniara turned her head, Im'os? <q>But why aren't you attacking me.</q></p>
    <p>Derik didn't expect that question, but paused and lightly replied, <q>When 
      I saw you lying on the beach, you seemed defenseless, like you needed protection.</q></p>
    <p><q>Me...</q> she scoffed, <q>need protection? HIGHLY unlikely!</q> and 
      narrowed her eyes at Derik.</p>
    <p>Dejected, Derik ruffled his eyebrows, <q>Would you have preferred that 
      I left you to freeze on the beach at night?</q></p>
    <p><q>Hah! I lived in Litor all my life. I, the Ice Empress of Litor created 
      that Ice! Do you dare challenge me to survive in that again?</q></p>
    <p>Derik's eyes widened, <q>You are the child of ice?</q> he backed away. 
      <q>I always thought the prophecy was concocted,</q> he snarled at his upper 
      lip, <q>And all this time you wanted to kill me?</q></p>
    <p>Iniara weakened; no longer had she felt the frozen spirit inside her control 
      her emotions. <q>Derik, wait,</q> she pleaded with him, <q>I can be different. 
      Never once have I wanted to attack anyone since I arrived here... in... 
      in your Daemenite land. Please, you have to believe me, don't throw me out, 
      I am defenseless... now.</q> She fell to her knees and stared at Derik from 
      his feet.</p>
    <p>His expression softened and he bent down to the ground to lift Iniara up. 
      <q>Nara... I believe you, the way you've talked to me is sincere, and I 
      can't help but...</q> he stopped and just looked at Iniara.</p>
    <p><q>Derik?</q> Merold called from the kitchen, as they were in the gathering 
      room of the small house, <q>Can you milk the cows and clean up a bit before 
      dinner?</q></p>
    <p><q>Of course, mother. Would you like to join me, Nara?</q></p>
    <p><q>Sure,</q> she replied wearily, she had never done such menial jobs before.</p>
