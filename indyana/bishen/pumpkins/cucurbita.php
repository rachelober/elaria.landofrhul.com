      <p>Cucurbita is a small season pixie (of the element Autumn) that tends 
        to the pumpkins of a far away pumpkin patch. She is primarily a solitary 
        creature with few friends besides the pumpkins that she tends to. She 
        can't communicate with them directly, perhaps she is too young still to 
        have this power, but knows exactly what they need to survive and thrive.</p>
      <p>She has limited attacks/powers because she is a non-violent being, hardly 
        any season sprites have the capacity for anger or revenge. Cucurbita is 
        especially pacifist and isn't very capable of sticking up for herself 
        either. Many times, she is unable to fight over the destruction of her 
        pumpkin patch at the end of the Halloween season, and instead quietly 
        watches and then picks up the pieces afterwards.</p>
      <p>When she meets Moschata, her feelings about her situation change as the 
        aged Bishen taught her about her own pumpkins and about the land. Her 
        own life's meaning changed as well as her anger over the humans that would 
        destroy her crop every year. When the autumn season begins to fade, Cucurbita 
        is sent to her yearly slumber, to awake in the late summer to begin tending 
        again.</p>
