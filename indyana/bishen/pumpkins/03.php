      <p>Cucurbita opened her right eye in a slit. She pulled her arms from their 
        defensive position and opened her other eye. Standing before her was a 
        leaf-green dragon form. It was only about the size of a large dog, but 
        to a sprite he was quite huge.</p>
      <p>*C'mon now, I have already determined you as no threat. With that, I 
        am no threat to you.*</p>
      <p>&quot;What?&quot; Cucurbita replied, where had the voice come from. She 
