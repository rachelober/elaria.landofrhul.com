        could I let this happen...&quot; she puttered away, seemingly forgetting 
        the &quot;horrible beast&quot; that followed her around.</p>
      <p>*You are quite the interesting creature, you know? Especially for a sprite. 
        Most have had the common decency of introducing themselves.*</p>
      <p>Cucurbita continued without even looking up. She wimpered a little as 
        she realized she would not have nearly enough water to finish the last 
        two rows of the patch.</p>
      <p>Having just about enough, the dragon wrapped his vine-like tail around 
        the sprite, *For such a silly little fairy, you sure are persistant in 
        what you do!*</p>
      <p>&quot;Ugh! Stop! Really now! I -REALLY- can't talk to you right now!&quot; 
