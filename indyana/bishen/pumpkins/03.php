      <p>*What? Are you daunted by my ability to communicate to you through telepathy?*</p>
      <p>&quot;Of course not...&quot;</p>
      <p>*For a sprite, being around magical creatures all the time, should be 
        quite use to this.*</p>
      <p>*wriggle* &quot;Actually, I am not, I live by myself... totally&quot;.</p>
