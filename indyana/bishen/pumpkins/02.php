      <h1>My September</h1>
      <p>It was late September in Northern Rhul, the air was getting crisp but 
        the leaves had yet to really turn to their lovely colors of red, orange 
        and golden yellow. Cucurbita has already woken up tonight.</p>
      <p>&quot;Work, work, work,&quot; she would sing until the sun would caress 
        the horizon and then she would dash off back to her tree hole to sleep 
        until the sun sat once again.</p>
      <p>This night was particularly cold, no clouds were in the night sky and 
        it allowed for the cold air to sweep by more ferociously. Cucurbita's 
        clothes, revealing at best, clung to the little sprite's body tightly. 
        Her teeth chattered as she walked down the vines of the pumpkins, taking 
        the time to water each one.</p>
      <p>The pumpkins were big and happy at this time. In August they started 
        to change to a lovely orange color, yet they were not ready to pick... 
        just yet.</p>
      <p>As she wandered down aisle after aisle, the trees creaked from the shrill 
        wind and a snap could be heard from the forest.</p>
      <p>Cucurbita jumped and spilled her watering can on the ground. &quot;Oh 
        snapping vines, what now?&quot; she complained to no one in particular. 
        She fell to her knees with a frustrated glare so the water would drown 
        the few pumpkins that surrounded the splash area. She then picked herself 
        up and dusted her skirt.</p>
      <p>As she leaned over to pick up the spilt watering can, a low growl huffed 
        from the same area as before. Cucurbita held her watering can close and 
        turned around slowly. The moonlight seemed to have lit up everything in 
        plain view. If she could see this area, so could a preditor. Without much 
        more hesitation. In one swift movement, Cucurbita dropped the watering 
        can once more, spun on her little, glowing sprite wings in a circle and 
        blew on the pumpkin patch with a heavy mist.</p>
      <p>Her small body fit nicely against one of the big pumpkins, she was only 
        about two feet tall after all. She was so nervous that her wings batted 
        constantly and gave off an sparkling, eerie white powder that filtered 
        through the fog she had created.</p>
      <p>&quot;It's just nothing,&quot; she muttered, as flashes of a terrible 
        dog or bear ravishing through the midnight air ran through her mind. Or 
        what about the haunting spirit that was suppose to be living in these 
        woods that the humans were always talking about? She finally calmed her 
        wings and the glow stopped, if the monster came any closer, at least her 
        wings wouldn't give her away.</p>
      <p>Then a perplexing sound came from darkness... a trill? &quot;P-p-please...&quot; 
        Cucurbita stuttered quietly, &quot;don't find me....&quot;. She held her 
        legs tightly against her body and rocked gently forward and backward.</p>
      <p>A green mist mixed with the gray mist that surrounded the pumpkins. A 
        soft bitter smell came from the green mist and made Cucurbita's nose wrinkle 
        and then it happened. She breathed heavily and with a quip, and sneezed 
        loudly.</p>
      <p>A snort came from the direction of the green mist and two glowing, white 
        eyes opened to Cucurbita's direction. &quot;Oh no...&quot; Cucurbita whined 
        as tears started to roll down her rosy cheeks. She silently fell to the 
        ground unconcious.</p>
