    <h1>Fixing Up</h1>
    <p>&quot;We are both very proud of you,&quot; Bright said to Pyyon as they 
      returned back to her cottage.</p>
    <p>*Thank you both for coming, it was wonderful to have your support*, Pyyon 
      replied.</p>
    <p>&quot;We will always be there for you, know that,&quot; Archun piped in.</p>
    <p align="center"><img src="images/hr.gif" alt="" width="261" height="40"></p>
    <p>Time had passed. Pyyon never had a real opportunity to go back to Eldora 
      and Bright and Archun warned her about the troubles there. They promised 
      her, however, that one day they would go back together to do some good.</p>
    <p>Bright Promise made herself busy as she knew she would by taking care of 
      all the local animals during this busy time of the season.</p>
    <p>Pyyon hid up in the trees whenever a customer arrived. Archun and Bright 
      thought that the Hearthans might be a little scared about seeing a dragon, 
      since they didn't live natively in Rhul. Reluctantly, Pyyon agreed to hide 
      while the people were there, sometimes hours at a time. She would spend 
      it gazing at her Branch of Plenty and dreaming of creating forests of food 
      for all of the farmers, even if they were scared of her.</p>
    <p>&quot;You can come down now,&quot; Bright called up to Pyyon.</p>
    <p>*Finally*, she muttered.</p>
    <p>&quot;I am sorry, Pyyon,&quot; Bright apologized. &quot;Really, just a 
      few more weeks, I swear and everything will quiet down, I promise.&quot;</p>
    <p>Pyyon frowned a little bit, *I want to believe you, Bright,* Pyyon shifted 
      around a bit, *but it will just happen again next year.*</p>
    <p>Bright frowned too, &quot;I know... I know... I ... I am trying my best... 
      look, maybe we can figure something out...&quot;</p>
    <p>She was cut off by Archun. &quot;Bright! Can you come here for a second?&quot;</p>
    <p>Bright turned in Archun's direction, &quot;Sure thing, Archun! be right 
      there!&quot; She turned back to Pyyon, &quot;I'll be right back, ok?&quot;</p>
    <p>*Ok...* she muttered.</p>
    <p>Bright Promise hurried to Archun's side and Pyyon watched carefully from 
      her perch. Archun looked very worried and as he talked to Bright a look 
      of fear covered her pale face. Pyyon got very worried herself. What could 
      it be? Was an animal sick? Was there trouble afoot? (She had none, so it 
      couldn't be that...)</p>
    <p>Soon Archun walked over to Pyyon while Bright Promise stayed by the cottage 
      with her arms crossed in front of her.</p>
    <p>&quot;Pyyon...&quot; Archun trailed.</p>
    <p>*Yes, Archun?* she flew down from the branches. *What's... wrong...?*</p>
    <p>&quot;This is really hard for me to ask of you...&quot; he paused and looked 
      off into the sky.</p>
    <p>*Anything, Archun...*</p>
    <p>&quot;I have to leave,&quot; he said abruptly.</p>
    <p>*What??* she quipped in return.</p>
    <p>&quot;Yes, I know, this is terrible. But something very important has come 
      up in the north and I have to tend to it quickly.&quot;</p>
    <p>Pyyon sniffled, *Take me with you...*</p>
    <p>&quot;I... you know I would but this is very dangerous and you are so precious 
      to me that I can't put you in this type of danger.&quot;</p>
    <p>*WHAT danger?* she sputtered.</p>
    <p>&quot;Shh shh...&quot; Archun tried to calm his bonded. &quot;It is something 
      that involves me and a few other people. I don't know when I will return, 
      but I promise you that I will.&quot;</p>
    <p>*You guys use that word a lot...* she sniffled.</p>
    <p>&quot;Promise?&quot;</p>
    <p>She nodded.</p>
    <p>&quot;Well, because I am not allowed to break promises. It is that simple. 
      When I mean something so dearly, I am going to do it.&quot;</p>
    <p>*What's keeping you from breaking a promise?*</p>
    <p>Archun looked away for a moment, contemplating his answer and Pyyon noticed 
      this. What she didn't know was all this time he had been keeping a secret 
      from her. For his own protection and for hers. Obviously, the secret was 
      that he was really a spirit, centuries old, but in a world like Rhul, discoveries 
      like that can mark you as a heretic and Archun was never one to really stir 
      the public.</p>
    <p>&quot;Pyyon, if you must know, I am more magical than I have ever led you 
      to believe. That is the short of it, but my life is an enigma of tangles 
      and I want to tell you all this, but for your safety I need to keep you 
      in the dark for now. I also need you to be strong. Can you do all this for 
      me, lovely?&quot;</p>
    <p>She hesitated for a moment, *Anything for you Archun*.</p>
    <p>Archun left that next morning, very early. Pyyon did not even see him leave...</p>
