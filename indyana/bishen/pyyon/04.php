    <h1>Brother's</h1>
    <p>&quot;So, have you thought about how you are going to take care of this, 
      Daddy?&quot; Bright Promise winked.</p>
    <p>&quot;Heh,&quot; Archun puffed, somewhat intimidated by this new task. 
      &quot;I hope I have time for him...&quot; he drifted in his speech.</p>
    <p>&quot;You'll make time. You'll see how easy it is, once you have something 
      to take care of, how you'll want to spend time on it.&quot;</p>
    <p>Archun smiled at Bright, she had the same attitude about caring for things 
      that Elaria owned. &quot;I am glad at least one of us has great expectation 
      for my caring of this dragon.&quot;</p>
    <p>&quot;Oh don't be silly, Archun!&quot; Bright smacked playfully at his 
      shoulder. He was much taller than her, so she had to extend her arm just 
      to tap it lightly. &quot;By the way,&quot; she continued, &quot;Did you 
      ever think of a name for your new baby?&quot; she smiled.</p>
    <p>Archun stopped and dug his toe into the soft ground beneath. He did decide 
      on a name, but he was not sure how Bright Promise would take it. &quot;Oh, 
      yes, a name.&quot; He continued walking, quicker this time.</p>
    <p>&quot;So?&quot; Bright Promise caught up to him and rounded him on the 
      right trying to stop him again. &quot;What's wrong, didn't you think of 
      a name?&quot;</p>
    <p>Archun stopped. He diverted his eyes from Bright Promise's worrying glare 
      to the trees above. He was not sure if he ever talked about Merik to Bright 
      Promise openly. &quot;Well, you see,&quot; he started, &quot;you know where 
      I came from, right?&quot;</p>
    <p>&quot;Of course, but if I understand it completely... no.&quot;</p>
    <p>&quot;When we first met, you saw the past in my eyes, I know you did. Did 
      you see other people there with you?&quot;</p>
    <p>Growing silent, Bright Promise pondered her thoughts from that day from 
      long ago. &quot;Seriously, Archun, how is this to help me?&quot;</p>
    <p>&quot;Just think for a second, please?&quot;</p>
    <p>Closing her eyes she remembered another girl with her one morning. She 
      had really silky hair that was trimmed just below her shoulder blades and 
      was very thin- short but thin. &quot;I remember a girl,&quot; she finally 
      whispered.</p>
    <p>&quot;Good, good... what did she look like?&quot;</p>
    <p>&quot;Short,&quot; she hesitated and opened her eyes, &quot;Light hair- 
      long, light hair and very thin build.&quot;</p>
    <p>&quot;Ahh, that's [NAME HERE], she was your sister.&quot;</p>
    <p>&quot;My real sister, or one of those people you said were my adopted siblings?&quot;</p>
    <p>&quot;Adopted... From what I know, you were alone before you met with Eldor.&quot;</p>
    <p>&quot;Oh,&quot; her voice drifted, slightly disappointed with the lack 
      of detail, though plentiful information, about her past life. Her eyes closed 
      once again. &quot;I remember three other boys,&quot; she smiled. &quot;One 
      was Aille. I'd never forget him,&quot; she nodded her head while still in 
      a trance.</p>
    <p>&quot;Aille is still with you, you know.&quot;</p>
    <p>&quot;Of course, Archun,&quot; she turned to face him and delicately opened 
      her eyes to his face. &quot;I would never back down from my religion, even 
      if this story you tell me is so outlandish.&quot;</p>
    <p>&quot;Believe me,&quot; Archun held her hands, &quot;I would never lie 
      to you.&quot;</p>
    <p>Their eyes met, a perfect blue with the swirling mixture of brown and gold. 
      &quot;I want to believe you, Archun.&quot;</p>
    <p>&quot;Then do.&quot;</p>
    <p>She tilted her head away from his stare. &quot;I believe,&quot; she whispered, 
      &quot;It's just hard to admit it to myself.&quot;</p>
    <p>Archun smiled and grabbed her close to him. &quot;I'm glad you said something.&quot;</p>
    <p>Bright laughed. &quot;So tell me, what is the name for the dragon?&quot;</p>
    <p>&quot;Merik.&quot; he paused.</p>
    <p>&quot;Merik, where did you come up with such a name?&quot;</p>
    <p>&quot;He was your brother.&quot;</p>
    <p>&quot;Oh... were you good friends with him...&quot; Bright questioned and 
      pushed herself slightly away from Archun's grasp.</p>
    <p>&quot;Kind of,&quot; Archun's voice drifted. <i>Should I tell her the real 
      reason? Would she understand?</i> &quot;He's the one that prompted you, 
      er Elaria, to talk to me in the first place. I owe him the deepest thanks...&quot;</p>
    <p>Bright cocked her head to the right and looked at him again. &quot;That's 
      so sweet...&quot; she started to giggle slightly but stopped herself.</p>
    <p>&quot;What, you think that's funny?&quot; Archun smirked jokingly.</p>
    <p>&quot;Indeed I do!&quot; Bright flirted.</p>
