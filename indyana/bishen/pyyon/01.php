    <p>&quot;B-b-bond mate? You mean this lad will rely solely on me?&quot;</p>
    <p>Bright nods her head, &quot;Of course, Archun! I wouldn't just pick anyone 
      to raise this dragon.&quot;</p>
    <p>&quot;Then why don't you?&quot;</p>
    <p>&quot;You have all the time in the world to raise him,&quot; she giggled, 
      &quot;you know, in between being a caretaker of Elaria and all.&quot;</p>
