    <h1>Archun, a bondmate?</h1>
    <p>Archun crept along the tall fence near Bright's house. In his feline form, 
      he could dodge the untrained eye and sneak up on his prey. There she was. 
      Long brown hair blew in the light breeze of the late morning over her slender 
      shoulders past the sleeves of her blouse. Bright cuddled something in her 
      hands and stared at what lay in her palms.</p>
    <p>Quite bored at just watching the girl, Archun slid underneath one of the 
      lower planks of the fence, past the prickly flower bushes to just near the 
      front step.</p>
    <p>Bright's home resided north of the large town, Hearth. All her life she 
      lived far away from the other people, yet everyday she traveled down the 
      long dusty path from her front door to the animal shelter in the town. On 
      her way, she would pass the orchards and playgrounds. Most of the time at 
      those points she would find children on their way to school, distracted 
      by the play things, and she would attract their attention and lead them 
      on their way to the primary school. Along the way, she would recite old 
      legends she learned while she was a little girl. The children, entranced 
      by her storytelling, hung on her every word and wept as she left them at 
      the school on her way to the shelter.</p>
    <p>By this time in her life, her parents had passed on. They were both weak 
      people, and in genetics she predicted she would be the same way. Therefore, 
      at an early age she decided that she would do something early on that would 
      make an indelible imprint in Hearthan society- which is where the shelter 
      came into play.</p>
    <p><i>*Meeeeeoooooowww!*</i></p>
    <p>Archun jumped from behind a post near the front door step where Bright 
      sat towards the young lady. Just as he was about to jump in her lap, she 
      moved her body a few inches right. Those few inches affected Archun's target 
      and he immediately skid directly into the wooden frame of the door face 
      first.</p>
    <p><a href="http://wayuki.curvedspaces.com/"><img src="images/brightpromise.gif" alt="Bright Promise" width="50" height="125" border="0" align="left"></a>&quot;Archun!&quot; 
      Bright Promise screamed, &quot;You scared me!&quot; as she instinctively 
      cuddled the object in her palms closer to her chest.</p>
    <p>Archun replied telepathically, *You let me hurt myself* as he carefully 
      rubbed his nose fondly with his front right paw, pouting slightly at Bright's 
      glare.</p>
    <p>The young girl's glare faded as a sly grin formed on her face, &quot;You 
      better be more careful, my love.&quot;</p>
    <p>The light blue cat shook his body with his black stripes gleaming in the 
      day sunlight as he crept closer to Bright. He rubbed his left cheek over 
      the lady's red skirt and purred softly.</p>
    <p>&quot;Come on, Archun. I don't have time to play with you. Besides, I have 
      a special job for you.&quot;</p>
    <p><img src="stages/egg.gif" width="75" height="100" alt="Bishen Egg" align="left">*Oh 
      no, what is it this time.*</p>
    <p>Bright Promise gladly withdrew her hands away from her chest and laid her 
      palms open to Archun's stare. For laying on top of her thin fingers was 
      a light green, yellow-spotted egg.</p>
    <p>&quot;This is a Bishen egg, Archun. I went through a lot of trouble to 
      get this by going to the Bishen Realm and getting permission to obtain an 
      egg. As far as I know, inside is a little male wyrm, because of the green 
      shell, and when he hatches he will become a powerful dragon that will be 
      able to use a breath weapon and magical object.&quot;</p>
    <p>*I believe you forgot the part where I play a role...*</p>
    <p>Her grin grew as she leaned closer to Archun. &quot;First, you need to 
      transform into your hearthan form.&quot;</p>
    <p>Archun sighed slightly, *but I enjoy receiving cuddles from cute girls, 
      I get a lot while in my cat form* he replied jokingly.</p>
    <p><a href="http://wayuki.curvedspaces.com/"><img src="images/archun.gif" alt="Archun" width="60" height="150" border="0" align="right"></a>The 
      cat stretched his forearms away from his hind legs as his body morphed outwardly. 
      His blue fur with black stripes retreated into his skin, replaced by light 
      blonde hair that covered his head and a few places on his body. His paws 
      grew into long fingers and toes with shorter, blunt nails and his skin returned 
      to its pale tone. His eyes blinked quickly as his diamond pupils transformed 
      into the hearthan oval and the blue pigment of its concentric circle glittered 
      in the sunlight. Luckily for him, he remembered to morph clothes onto his 
      body this time. He stood upwards tall, towering over Bright Promise.</p>
    <p>&quot;Thank you dear.&quot; Bright stood to speak clearly to Archun.</p>
    <p>&quot;Now what does this egg have to do with me.&quot;</p>
    <p>&quot;As you know, we here take it upon ourselves to help the less fortunate. 
      I had to go through a great lot of trouble to get this egg. That's why I 
      want you to take care of him.&quot; She held out her hands for Archun.</p>
    <p>&quot;Me? I can't even keep your orchard lilies alive while you are gone 
      for a weekend. How do you expect me to raise a dragon? An obviously, <i>rare</i> 
      dragon!&quot;</p>
    <p>&quot;Oh, I trust you Archun! You aren't nearly as bad as you think you 
