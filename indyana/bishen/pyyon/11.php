    <h1>Travel to the North</h1>
    <p>Much time passed after Archun had left. Seasoned had changed and Pyyon 
      had saw many animals crawl to Bright Promise's doorstep looking for care. 
      She began to feel that she didn't feel welcome there anymore. Bright had 
      much to do with her own time, plus, she was just her caretaker. Her real 
      bondmate was off on a mission of sorts.</p>
    <p>*Bright*, Pyyon finally confessed one day, *I am leaving, I am bored with 
      this, I need to find Archun.*</p>
    <p>Bright stopped bathing a bunny Cyen and looked up. &quot;Are... are you 
      sure?&quot; and wiped her forehead with her arm trying to get her wet hair 
      out of her eyes.</p>
    <p>*I'll never do anything here...*</p>
    <p>Bright Promise dried her hands on a nearby towel and let the little bunny 
      escape to her brothers and sisters. &quot;I know how much you miss Archun, 
      but... what am I kidding. I knew you'd want to eventually, maybe even just 
      leave without telling me.&quot;</p>
    <p>*I would always tell you, well of course unless you fought me about going.*</p>
    <p>&quot;Heh,&quot; Bright smiled, &quot;I only fight if I think I have a 
      chance at winning.&quot;</p>
    <p>Pyyon nodded lightly about this, *I am confident about this.*</p>
    <p>&quot;Well then, I approve but I do advise that you do not leave until 
      the weather calms down in the north, that should be in a month or so.&quot;</p>
    <p>*Really?*</p>
    <p>She nodded, &quot;Unfortunately so, if you wait another month I can guarantee 
      that you'll have better flying weather.&quot;</p>
    <p>*Well,* Pyyon snuffed, *Guess I'll just have to slither the whole way!*</p>
    <p>They stifled a laugh for a moment but burst out laughing, &quot;Yeah, I 
      would like to see you try!&quot;</p>
    <p>*You're right, that would be entirely too silly.*</p>
    <p>&quot;Hmm,&quot; she giggled, &quot;would you like to come with me on a 
      little journey out west? It'll only be a few days. I think you're looking 
      to get out of the Hearth area anyway.&quot;</p>
    <p>*Oh, of course, anywhere but here.*</p>
    <p align="center"><img src="images/hr.gif" alt="" width="261" height="40"></p>
    <p>Bright Promise had an errand to run out to the west. Apparently, there 
      were some animals that she brought back from another realm that had run 
      rampant, had many children and were killing a lot of the natural plants.</p>
    <p>&quot;Look BP,&quot; a big sturdy man began to snort at the girl, &quot;We 
      agreed to let you keep these... cats here for as long as you'd like under 
      the condition that you would have them in check at ALL times. This is unacceptable.&quot;</p>
    <p>&quot;I know, I know...&quot; she replied in a hustle as she weeded through 
      her bags looking for tools to tame the wild beasts.</p>
    <p>The man followed her along through the forest as she began looking under 
      bushes and trees for the creatures but being very unlucky. &quot;Now see 
      here, I demand that you give me some sort of agreement that this will be 
      taken care of.&quot;</p>
    <p>Bright stopped and turned quickly to the man, &quot;I'm here aren't I?!&quot; 
      and whipped around again and continued her search.</p>
    <p>&quot;These buggers are running absolutely loose! They make noises in the 
      night and scare the children. I mean, we really have to watch out about 
      our livestock, they could get at them at anytime.&quot;</p>
    <p>Bright stopped and shut her eyes and gave a silent sigh before turning 
      to the man again, &quot;They are herbivores, sir, I can guarantee your livestock 
      is perfectly safe.&quot; She turned around again and returned to her work, 
      muttering, &quot;I'd be more worried about your vegetables.&quot;</p>
    <p>The man quickly shut up and silently followed.</p>
    <p>In the meantime, Pyyon had been flying overhead and tried to keep out of 
      the way of the stupid man that was with Bright. The air was crisp and clear 
      and and a few puffy clouds hung in the sky. She would try to reach out and 
      touch it with her nose but it was always just a little further off and she 
      could never grasp it and then it would disappear into the sun.</p>
    <p>Bright was hunting a few cat like creatures that had taken over a part 
      of the forest near a little village west of Hearth. Pyyon probably should 
      have been more careful about being out in the blue like she was around country 
      folk, they usually didn't like a lot of new things flying around, but Bright 
      assured her that this place was different from the Town. They were very 
      willing to allow Bright to release animals near their forest for the propagation 
      of the species... except they seemed to like this place too much.</p>
    <p>The animals began to eat away at a lot of the vegetation of the forest, 
      so much that other creatures were being starved and scared out of the forest. 
      Bright had called in a favor to a person in the originating realm and they 
      agreed to take a lot of the animals in. The only problem now was catching 
      enough so that balance could return to the forest. She also had to think 
      of a way to give them a natural predator so they didn't just build up their 
      numbers again. Bright hated how she played with the environment so much, 
      but she was generally very intelligent about it, sort of an innate understanding 
      to how living things... well, lived.</p>
    <p>&quot;See, I calculated that we've caught about 50% of the cats in this 
      forest, if we can get another 10% by the end of the day, we will be in good 
      shape to bring them back.&quot;</p>
    <p>Just then, Pyyon saw a flash of color come from on the ground. She quickly 
      alerted Bright Promise, *Quick! Off to your right! A really big one!*</p>
    <p>&quot;Hey all!&quot; Bright Promise rallied up her helpers, &quot;Pyyon 
      says there is one up that way! Let's get too it!&quot;</p>
    <p>She had a bunch of burly men with her that were very capable of taking 
      down a cat by themselves, however this one looked like it might take a few. 
      It was probably a full grown male.</p>
    <p>The group managed to corner him by a wall. He was a big boy. Bright couldn't 
      remember how old this one was but he probably came over in the first batch 
      of cats and wasn't a bred one.</p>
    <p>&quot;I guess I will do a check on his tag once we secure him. Ok boys, 
      do your job, just don't you dare hurt him!&quot;</p>
    <p>&quot;Yes Ma'am,&quot; they all nodded and went to tackle the poor beast.</p>
    <p>Pyyon swooped down next to Bright. *Is this how you deal with all your 
      out-of-world creatures?*</p>
    <p>&quot;Oh of course not, don't get all defensive. These cats were meant 
      to be wild, so we have to be wild with them.&quot;</p>
    <p>*I guess you're right.*</p>
    <p>&quot;They are not very intelligent beings either, they never listen to 
      reason. 'Please mister kitty, get in the cage so we can inject you with 
      a sedative?' no, they don't usually go for that.&quot;</p>
    <p>*Heh, I guess so.* Pyyon watched on, the men tackled with the cat as he 
      lashed out with his left paw as another man held down his right and yet 
      another man went for his back legs. They were all dressed in a heavy fiber 
      so that they would hopefully come out with minimal scratches. Out in this 
      wilderness, it wasn't too uncommon for jobs like this to be done. Many times 
      it involved livestock or other breeding animals. Finally, they managed to 
      hold him down long enough for a shot that would put him to sleep until he 
      was safely transported out of the realm.</p>
    <p>&quot;Well, I've done my job here,&quot; Bright talked to the leader of 
      the village, a short man but with a ego as wide as his belly.</p>
    <p>&quot;And that you did, Ma'am. I hope they find it... heh... restful on 
      their journey home.&quot;</p>
    <p>&quot;Ah, I wil see to that Master Sherrif,&quot; she replied with a polite 
      nod.</p>
    <p>Bright walked away with her workers now traveling to the realm. Luckily, 
      a portal was nearby, maybe only an hour or two of walking, until they could 
      leap over to the other realm.</p>
    <p>*What a mess,* Pyyon commented.</p>
    <p>&quot;Ah, it went quite smoothly. Our next job will be to get through customs 
      in the Faerly. They aren't going to want these back, you know,&quot; she 
      rolled her eyes. &quot;People can be so argumentative sometimes,&quot; she 
      turned to Pyyon that was gliding next to her, &quot;you'll have to learn 
      that quick if you decide to work with anyone in the near future... you'll 
      always disagree about something.&quot;</p>
