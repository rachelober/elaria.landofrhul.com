    <p>Bright Promise is an eccentric woman with many goals in her life. Her biggest 
      project has been her Animal Care Center in Hearth that caters to animals 
      of all stature. She has studied long and hard about the crazier species 
      of animals found in Rhul and it seems she has been practically everywhere. 
      During her travels in Rhul, she was able to track done certain portals around 
      Rhul and successfully traveled through some of them to other worlds. Through 
      these passages she was able to network with other lands and help set up 
      Animal Rescue services for lands with endangered species. In this program, 
      leaders from other worlds would join together and assign applicants creatures 
      to take to good homes in hopes of propagating the species.</p>
    <p>Over the years, Bright Promise housed many species in Rhul in the different 
      areas and many times had to try and hide them from locals who may try to 
      hurt these other animals. Finally, after many years she was able to get 
      government sanction for her work and was given a piece of land to house 
      these endangered animals.</p>
    <p>In this story, Bright Promise travels to Bishen Realm and brings back a 
      mysterious egg. Her close friend, Archun, is asked to take care of it. She, 
      promises to help him along the way and believes that he would be an excellent 
      bondmate to a dragon.</p>
    <p>She treats Pyyon like a daughter and loves her dearly. After Archun leaves 
      on urgent business, Bright Promise cares for Pyyon.</p>
