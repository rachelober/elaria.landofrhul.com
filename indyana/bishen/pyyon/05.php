    <h1>The Hatching</h1>
    <p>&quot;Archun,&quot; Bright said singsong, &quot;I really think you should 
      be spending more time with the egg!&quot;</p>
    <p>&quot;I am...' how much more time do you expect me to spend with this... 
      thing?&quot; Archun held up a small knitted bag that held the egg. &quot;I 
      already carry this stupid thing everywhere I go... even when I... you know...&quot;</p>
    <p>&quot;Heehee,&quot; Bright smirked. &quot;I just think that the more tie 
      you spend trying to treat the egg as a being, the closer you two will be 
      whenever he is born!&quot;</p>
    <p>&quot;Bright, that may work when you're catering to pregnant Cyen...&quot;</p>
    <p>&quot;No seriously,&quot; she stopped working in her garden which they 
      had been sitting in for the afternoon, &quot;You know me, and you know my 
      business, you'd think you'd understand that I knew what I was talking about!&quot;</p>
    <p>&quot;Of course you do,&quot; archun replied. He stood up and stretched 
      his arms behind his head and turned away to face the fading sun. &quot;I 
      think I'll go for a walk.&quot; Archun began to walk through the little 
      gate that led to the southern path that flowed into the main artery into 
      Hearth.</p>
    <p>&quot;Don't forget your EGG!&quot; Bright called. Archun face palmed and 
      held out an arm without turning his head, &quot;Oh, how could I forget...&quot;</p>
    <p align="center"><img src="images/hr.gif" alt="" width="261" height="40"></p>
    <p>Archun walked for a bit off the path. He started heading north with no 
      particular place to go. He'd been everywhere and nothing seemed like it 
      was new to him. The little bag that bright knitted for Merik dangled around 
      his lanky neck. It was knitted with a cyan blue and pink string. Bright 
      just loved making senseless objects, and Archun played along.</p>
    <p>It was so hard dealing with Bright. She seemed to ignore the troubles in 
      her life by creating cute things to distract her. She was an expert in her 
      field of study, the upbringing and care of animals in local Hearth, and 
      loved doing it. Residents from all over the neighboring villages and town 
      flocked for her advice on raising stock animals to caring for household 
      pets.</p>
    <p>Lately, he noticed that she complained more about the care of the vegetation 
      around the areas. For the sake of the animals, she began researching other 
      lands in Rhul.</p>
    <p>She found that with the high population in Auste and the neighboring Eldora 
      cities and landscapes, many of the natural animals that made their homes 
      there were losing their food sources and available land.</p>
    <p>Eh, he saw it coming. He remembered Aille telling him once that this was 
      they way things went. Old books don't spoil, but the binding gets old and 
      harder to keep the pages together. Maybe this was what was happening with 
      Eldora. He remembered how the sun use to set in the east and how shadows 
      would cast themselves on the walls of the great mountains. Today, the sky 
      was perpetually dark gray and tasted of smoke.</p>
    <p>The Seques were very intelligent beings, they knew this but this never 
      stopped them from creating more, wasting even more and doing little to stop 
      their expansion. He could not even believe that over the many generations, 
      flying was banned. Why? They believed it was arcane. He rolled his eyes, 
      the creatures had wings for Aille's sake!</p>
    <p>The larger moon had risen in the sky and a light blue light shone down 
      on the area. He had made his way to a large field. He groaned thinking of 
      walking all the way back to Bright's cottage, but then mentally slapped 
      himself and transported back to the porch.</p>
    <p align="center"><img src="images/hr.gif" alt="" width="261" height="40"></p>
    <p>&quot;Archun!&quot; Bright Promise hissed, &quot;Where've you been? I've 
      been calling your name for hours!&quot;</p>
    <p>&quot;I've been walking! I don't need you keeping tabs on me constantly!&quot; 
      he retorted, he liked his independence, he always had and he wasn't about 
      to change that.</p>
    <p>Bright rolled her eyes, &quot;C'mon! We are going to travel to Eldora right 
      away!&quot;</p>
    <p>&quot;Why?&quot; Archun cocked an eyebrow.</p>
    <p>&quot;Do I need to explain? A traveler came into Hearth today while you 
      were gone and told terrible stories of the conditions of the landscape. 
      We can do something, don't you understand?&quot;</p>
    <p>&quot;I understand, but what do you expect me to do?&quot;</p>
    <p>&quot;Well,&quot; she approached the spirit, &quot;first, transport me 
      to the city of Auste.&quot;</p>
    <p>Without hesitation, Archun grabbed Bright's shoulders and they were transported 
      to the city.</p>
    <p align="center"><img src="images/hr.gif" alt="" width="261" height="40"></p>
    <p>In the days following their arrival to Eldora, Archun and Bright spent 
      most of their time in the countryside, or what was left.</p>
    <p>&quot;If I can find some way to restore this landscape, I would feel fulfilled.&quot;</p>
    <p>&quot;Hon,&quot; Archun turned to her as they walked with a few flowers 
      in their hands that they had decided to plant, &quot;You say this about 
      a lot of things...&quot;</p>
    <p>&quot;Yes, and I can't stop until I feel I've made it right.&quot;</p>
    <p>Earlier that day they had spent their time assessing the damage to the 
      countryside. Many trees had been destroyed and they had no way of replenishing 
      the entire landscape alone. They were unable to illicit much help from the 
      city itself, but the neighboring Taeds that lived on the countryside were 
      eager to figure out some ways to help their failing land.</p>
    <p>Bright and Archun finally reached their destination. They had chosen to 
      start on the most eastern corner of the mountains that overlooked the great 
      northern sea. Life still bloomed here and they thought it was their best 
      bet to try to revive the plants that still existed and try to move it outwards.</p>
    <p>&quot;Ah, the simple life.&quot; Archun smiled. He asked for such a life 
      again. After searching with a life consumed for hatred of his previous life, 
      archun welcomed such minimal tasks.</p>
    <p>&quot;I knew you'd like something like this,&quot; bright replied, digging 
      her hands deep into the soil, tilling the land so that the flowers would 
      have the best sitting place.</p>
    <p>A few Taeds wandered the landscape, looking confused and helpless trying 
      to figure out how to plant things. Bright giggled in her usual tone, &quot;I 
      think i will have to work with them next on how to take care of their belongings!&quot; 
      she diverted her attention from the plant and noticed Archun, &quot;Why 
      the queer look on your face?&quot;</p>
    <p>&quot;Uhh, I think the egg is moving?&quot; He put down the plant he was 
      ready to plant. The bag that bright had made was hung around his neck, as 
      always, and was shifting slightly. He carefully used his thumbs to lift 
      the strap from around his neck to bring the egg to the ground.</p>
    <p>&quot;Oooh, how wonderful! You really have taken care of the egg well!&quot;</p>
    <p>&quot;What? All I did was carry it with me everywhere!&quot; He looked 
      down with a slight grimace and expression of &quot;Ok, what do I do now?&quot; 
      as a few cracks appeared on the surface. &quot;C'mon egg, hatch already!&quot;</p>
    <p>&quot;Shhh... don't yell at him, c'mon, we'll finish planting and by the 
      time we're done, he'll be ready to pop out of his shell.&quot;</p>
    <p><img src="stages/wyrmd.gif" alt="Pyyon" width="149" height="95" align="left">They 
      decided to to place the egg in a neat patch that they had begun to plant. 
      Every 15 minutes or so they checked back and the little wyrm had moved more 
      and more out of the egg until finally its entire body lay tired on the pink 
      flowers.</p>
    <p>&quot;How darling,&quot; Bright whispered as they packed up their tools 
      for the evening. &quot;Go ahead, pick her up.&quot;</p>
    <p>Archun lifted the little creature in his hand as it breathed heavily. &quot;Whoa, 
      wait a minute, you called him a her.&quot;</p>
    <p>&quot;Well of course, it is a girl after all.&quot;</p>
    <p>&quot;No way&quot;, he said. &quot;The egg was green, it was a male!&quot;</p>
    <p>&quot;Obviously, nature has a way with playing with our minds, no?&quot; 
      she held the wyrm up, publicly displaying that it WAS a little girl he was 
      holding.</p>
    <p>&quot;Well I'll be an old hammock. What do we do now? The name I chose 
      has no business for a girl.&quot;</p>
    <p>&quot;I think that's you to decide. What would you name her instead?&quot;</p>
    <p>&quot;I don't know! It took me long enough to decide on the first name!&quot;</p>
    <p>&quot;Think of something!&quot; she retorted, &quot;What do you think of 
      when you think of her?&quot;</p>
    <p>&quot;Gender confusion.&quot;</p>
    <p>&quot;Gah! Let's be serious here!&quot;</p>
    <p>&quot;Alright, alright.&quot; Archun scanned the landscape and his eyes 
      fell on the broken shell pieces by the flowers. &quot;I think pink.&quot;</p>
    <p>&quot;Pink?&quot;</p>
    <p>&quot;Yes, pink. She was born on pink flowers,&quot; he pointed with his 
      free hand to the area he watched.</p>
    <p>&quot;That's a good start, so the name is Pink?&quot;</p>
    <p>&quot;No, I'll name her Pyyon, or Pink in the old language. It is much 
      more elegant than what you Hearthans speak.&quot;</p>
