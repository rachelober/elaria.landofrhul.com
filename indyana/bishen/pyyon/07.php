    <h1>Welcome Home</h1>
    <p>Time had passed quickly for the little family. It was soon decided that 
      they were unable to stop the decay of the Eldorian landscape too much more. 
      Pyyon had grown to a shrape and her wings had grown to full size. Archun 
      was very proud to have an arboreal dragon to raise. Pyyon had helped them 
      so much by trying to get the Taeds to help with the growing he felt bad 
      that they had to leave without accomplishing much. Bright had other pressing 
      matters back in Hearth and with the upcoming Harvest season, it was terrible 
      to leave farmers without a local animal keeper around to check on their 
      animals.</p>
    <p>Pyyon could not help much with the planting of trees, it was not in her 
      best interest to try, seeing as she did not have claws. She instead gave 
      encouraging feedback to the others and cared for little Taeds while their 
      parents worked. It was expected she would feel some sort of frustration 
      for not being able to help too much. At least she was able to fly around 
      from plant to plant, carrying a watering can, and sprinkle the liquid down 
      to the roots.</p>
    <p>She did love plants, some days she would just sit by them and talk to them 
      quietly. Archun and Bright Promise looked on, never hearing what she said 
      and if she caught them looking her way as she did this, her eyes would narrow 
      and she would huff and take off into the sky to find a better place to stay.</p>
    <p>*Hey you*, she would whisper to the plants, *grow already, you know I am 
      getting quite tired of your attitude. Look at all these people trying to 
      help you out and all you can do is whine for more water. Pssh, look at the 
      plant next to you, she's probably twice as big as you, and what are you 
      doing? Sun bathing! Sheesh.*</p>
    <p>Although she was playing, she secretly wished that her words would somehow 
      ring true with the plants.</p>
    <p align="center"><img src="images/hr.gif" alt="" width="261" height="40"></p>
    <p>&quot;Umm, Pyyon?&quot;</p>
    <p>*Yes Archun?*</p>
    <p>&quot;Can you come here a moment, Bright and I would like to talk to you 
      about something.&quot;</p>
    <p>*Sure!* Pyyon quickly flew over from her perch and landed in front of the 
      two. Bright looked completely worn and her hair was an utter mess, which 
      wasn't too uncommon, but more messy than usual.</p>
    <p>&quot;Pyyon,&quot; Bright quietly said as she furrowed her brow. &quot;It 
      seems the situation here in Eldora has gotten worse. For whatever reason 
      the sky isn't as bright and the plants are dying very quickly. The Taeds 
      are becoming extremely superstitious and Archun and I both believe we would 
      all be much safer if we moved back to Hearth as soon as possible.&quot;</p>
    <p>*What, w-what do you m-mean... s-superstitious?* she sputtered.</p>
    <p>Archun bent down to Pyyon's height, &quot;They aren't looking too fond 
      on us newcomers. They believe that by bringing a dragon onto their mountainside 
      has cursed them. Since dragons are the Seques' enemy, they believe, Aille 
      knows where, that they are being punished fiercely for their transgressions.&quot;</p>
    <p>Pyyon's face fell. *How could they feel that way about me?*</p>
    <p>Archun shrugged, &quot;I wouldn't take it personal, Pip. Apparently they 
      don't like gryphons either b-&quot; he trailed, reminding himself quietly 
      that he hasn't told Pyyon about himself in too much detail, yet.</p>
    <p>*Hmm, I never met a gryphon before, are they mean?*</p>
    <p>&quot;Oh yes, um... deadly...&quot; Bright smirked.</p>
    <p>&quot;I would hope so,&quot; Archun rolled his eyes.</p>
    <p>*Well, umm... I w-would a-agree with your a-assessment of the s-situation.*</p>
    <p>&quot;Thank you, Pyyon.&quot; Bright smiled softly, &quot;We will leave 
      in the morning then, that sound alright?&quot;</p>
    <p>*Y-yes. Quite.*</p>
    <p align="center"><img src="images/hr.gif" alt="" width="261" height="40"></p>
    <p>The trio returned home, well, Pyyon went to her new home. She gazed around 
      the open lot and saw many trees and plenty of living things crawling about. 
      Plus plants. Oh yes, many different kinds of plants that she had never seen 
      before nor even imagined during her short life span.</p>
    <p>*Oh, Bright. Th-these are lovely.*</p>
    <p>&quot;I am glad you enjoy them, Pip&quot; Bright grinned. &quot;They are 
      all yours then!&quot;</p>
    <p>Pyyon's eyes glowed, *R-really?* Her heart began to race inside her, this 
      was the best gift she could receive.</p>
    <p>&quot;Of course, you know, you did a great job up in Eldora, plus I'll 
      be busy with the farmers, you've earned it. Just remember to water them 
      on schedule.&quot;</p>
    <p>*Of course... of course...* Pyyon nodded.</p>
    <p>&quot;Here, let me show you around my home.&quot; she pointed directly 
      to her house, &quot;That is where I live. I own the home and my family has 
      lived there for a few generations, after living more south for the other 
      generations. So relatively, it is a new home. I think it is probably not 
      going to suit you very well... seeing as how you fly around.&quot;</p>
    <p>*Ah, not another worry... I will reside outside.*</p>
    <p>&quot;Perfect, I hope you don't mind?&quot;</p>
    <p>*Not at all... as long as I can find a tree...*</p>
    <p>&quot;Well then, let me show you this.&quot; She motioned Pyyon to follow 
      her even though Pyyon immediately shot up into the air to take a look around.</p>
    <p>Behind Bright's cottage was a little orchard of trees, perfect for perching 
      in. *Oh my,* she marveled. *Absolutely wonderful for me.*</p>
    <p>&quot;How do you like it?&quot; Bright shouted up to Pyyon. Pyyon nodded 
      with a delightful grin, noting her approval and flew down to the ground 
      again. Bright stood by the front of the walkway to her cottage. &quot;And 
      here we have the mailbox. Which I am sure is overflowed with late mail,&quot; 
      she said as she grimaced.</p>
    <p>Opening it, the letters inside spilled to the ground. One particular letter 
      in delicate and beautiful paper caught Pyyon's eye. *What's that one?* she 
      pointed to as she flicked away the other letters.</p>
    <p>&quot;Hmm, let's have a look.&quot; Bright picked up the letter and automatically 
      identified the seal to be from the Bishen Realm. &quot;Oh, damnit, how could 
      I forget!&quot;</p>
    <p>*Bright*, Pyyon scolded, *you always yell at Archun when he talks like 
      that*, her eyes narrowed.</p>
    <p>&quot;Sorry,&quot; Bright Promise frowned. &quot;but this is important 
      for you Pyyon.&quot; Bright continued as she sliced the letter open quickly.</p>
    <p>*Why, what is it?*</p>
    <p>&quot;I forgot you are still a shrape. For Bishel dragons, you need to 
      attend a ceremony in front of your parents and siblings so that the Kailan 
      can pronounce you a full Bishen.&quot;</p>
    <p>Pyyon got suddenly nervous. *I-infront of... what...*</p>
    <p>&quot;Don't worry,&quot; Bright put her hand on Pyyon's snout and rubbed 
      gently, &quot;it is a easy task to do, you don't even need to talk, the 
      Kailan will do everything.&quot;</p>
    <p>*Oh... ok*, Pyyon thought to herself, *I haven't even met another Bishen 
      before, and now I have to go back to the Realm. What do I have to show for 
      it? Nothing!*</p>
    <p>&quot;Don't look so glum, really now. We will have fun. We will even make 
      Archun come with force,&quot; she winked to Pyyon.</p>
    <p>Archun stood off to the side unpacking things and overheard the conversation. 
      At that remark he crossed his arms and rolled his eyes with a sigh, &quot;Really, 
      you girls don't give me any credit. You wouldn't even have to ask me and 
      I am already there for you Pyyon.&quot;</p>
    <p>Pyyon giggled, *Of course*, Archun. *I know I can count on you.*</p>
