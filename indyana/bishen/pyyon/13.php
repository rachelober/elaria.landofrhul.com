    <h1>Good-Bye &amp; Hello</h1>
    <p>The wonderful bandits that traveled to the other Realm returned slightly 
      beaten and torn and in desperate need for rest. Too bad they weren't getting 
      paid beans.</p>
    <p>&quot;What a horrible mess,&quot; Bright confided in Pyyon. &quot;I've 
      been in bad situations with other realms but this one was pretty bad... 
      no one... died... before.&quot; She held her head low.</p>
    <p>Pyyon wrapped her tail around her friend, *I am sure they are ok now... 
      wherever they are and they forgive you.*</p>
    <p>Bright looked up with a few tears in her eyes. &quot;Thank you...&quot;</p>
    <p>Pyyon hugged Bright.</p>
    <p>Later that evening, Pyyon said her good-byes to Bright Promise and the 
      rest of the animals that lived around her cottage.</p>
    <p>Pyyon took to the air and saw the land as it was meant to be seen...</p>
    <p>The only object she brought was her was the Branch of Plenty, anything 
      else she needed she could find along the way. She admitted to herself that 
      she was scared, but a good scared- one that makes you excited that you are 
      a part of and keeps you driving to finish your errand.</p>
    <p>On her flight over the world, she saw many things, both good and bad, that 
      reminded her of her own mortality. She assured herself, she would live a 
      long and happy life... but to live a long and happy life she needed to spice 
      it up a little.</p>
    <p>There was a farm, not too far from Hearth, that looked like it was down 
      on its luck. Pyyon had rested in a tree near the farm's fields and watched 
      with keen eyes as the farmer and his wife comiserated concerning the lost 
      crop.</p>
    <p>&quot;I... don't know if we can stay in buisness, ma.&quot;</p>
    <p>&quot;Pa, what about our children? If we are broke, they can't take care 
      of us either. We'll lose the farm, everything!&quot;</p>
    <p>The old man crossed his left arm under his right elbow and leaned on his 
      right hand with his head. He looked over the field one more time before 
      bowing his head and said, &quot;Let's go inside, ma.&quot;</p>
    <p>Pyyon couldn't believe such harsh acts of nature could go this to good 
      families. She pulled out her Branch of Plenty and gazed at its perfect leaves 
      and the few fruits that grew on it. If she used it here, would she get in 
      trouble? Was she playing with something she shouldn't touch? She decided 
      to through caution to the wind and jumped from her spot in the tree to the 
      field below.</p>
    <p>She placed her branch on the ground and out spran different plants of all 
      shapes in sizes ranging from huge pumpkins and melons from the direct path 
      of her branch to small cherry tomatos where her power had ended. Her energy 
      was spent. The farmer would be back early tomorrow morning, but perhaps 
      she could rest in the trees until the run rose. She dragged herself to one 
      of the tallest branches of the trees and lay there, quickly falling asleep.</p>
    <p>With the first light of the morning, Pyyon was off to the skies again, 
      going north to where she felt Archun would be.</p>
    <p>It took nearly a week for Pyyon to fly to the Alsan area of Rhul. A stop 
      here, a rest there and a drink of water had delayed her from getting there 
      earlier, but at least she was not incredibly weary from her travels. She 
      settled near a village, but far enough away so that she wouldn't be seen.</p>
    <p>A path ran from the village into the woods. The path was worn well and 
      looked like it had been used recently by many people. On this day, a few 
      families we walking down the path. Pyyon smiled as the children hopped from 
      leg to leg with anticipation for the event that was happening next.</p>
    <p>*Hmm...* Pyyon thought, *What could this be?*</p>
    <p>She followed them, hopping from branch to branch, tree to tree, until she 
      got to a clearing. Within that clearing was a pumpkin patch. And beautiful 
      pumpkins they were! Many people were stepping around, gazing at the pumpkins 
      with joy in their eyes as they tried to find the biggest, orangest pumpkin 
      there was!</p>
    <p>Then, out of the corner of her eye a green mist emerged from the other 
      side of the forest. It was thick and heavy. She cocked her head in confusion 
      and watched in horror as the people in the patch began to swarm to the pathway 
      to get away from the dark mist that approached them.</p>
    <p>They tumbled over each other and some of the smaller children were pushed 
      out of the way by bigger children and trampled over by large feet. Pyyon 
      shut her eyes, but the screaming... the screaming was deafining. Then it 
      was quiet. The parents had picked up their children and ran away. And now 
      no one was left. For a few minutes, Pyyon sat on a branch in utter horror 
      of what just happened with her mouth hanging wide open.</p>
    <p>*What the- What was that?*</p>
    <p>A sparkle flittered in the distance of where the mist originated, which 
      had dispersed by now. Pyyon narrowed her eyes at this pixie thing. How could 
      something that small make such a mess. She looked closer and saw that this 
      pixie was not alone. Out from behind a large rock came out a large green 
      terran male.</p>
    <p>Pyyon was even more surprised now! With her wide jaw wide open came a pair 
      of huge eyes.</p>
    <p>The pixie was obviously upset at the middle-aged Bishen as she shook her 
      body around and grew red with anger.</p>
    <p>The dragon began to walk away and the pixie followed still angry and still 
      waving her arms in the air. The green terran finally turned around and probably 
      said something to the pixie, but Pyyon could not hear it. Finally, the pixie 
      calmed down and when the terran turned his body back to the path, the pixie 
      flew off into the woods.</p>
    <p>The terran moved along down the worn path. He didn't know where he was 
      going but he needed some solice.</p>
    <p>Pyyon jumped from branch to branch overhead and made an audible snap when 
      she landed on one of the branches. She halted, looking at the terran to 
      see if he noticed. He looked up to where the sound came from, but just turned 
      his head again and walked on.</p>
    <p>*Whew,* Pyyon breathed. She didn't want to get in the path of this dragon. 
    </p>
