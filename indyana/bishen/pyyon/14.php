    <h1>Fixing the Pumpkin Patch</h1>
    <p>Pyyon stayed in this area for the night in one of the trees. She gazed 
      down longingly at all the destruction within the pumpkin patch. She believed 
      that she had rested up enough that she could go down and see what should 
      could do about the plants.</p>
    <p>Row by row, Pyyon assessed the vine. It looked like it had been badly torn 
      and many pumpkins were fully grown already but smashed into the ground by 
      many running and tumbling feet.</p>
    <p>Once again, she pulled out her Branch of Plenty and row by row, restored 
      the life of the vine and many pumpkins sprung from the vine to healthy, 
      large gourds.</p>
    <p>That made her happier, she had fixed up the patch. But there was still 
      the fence that surrounded the patch that had been beaten down by people 
      jumping over the posts. That, she could not fix.</p>
    <p>A voice drifted through the wind behind her. *You know, they will just 
      be back next year.*</p>
    <p>Pyyon turned around and there the middle-aged Bishen stood, looking dejected. 
      *I... I...*</p>
    <p>*Every year, people come to this patch when the pumpkins are fully grown 
      and rip them from their homes so that they can bring them to their porch 
      for one evening to light. The next morning, the pumpkins are in the trash, 
      melted and mutilated.*</p>
    <p>Pyyon began to sweat, as much as a dragon could, and tried to back away 
      from the approaching Terran. *I, don't know anything of these strange human 
      rituals! I swear!* she pleaded.</p>
    <p>The Terran halted and looked at the female dragon. *Was I scaring you?*</p>
    <p>Pyyon moved back still without saying anything.</p>
    <p>The Terran looked even more glum, but changed the subject. *Those pumpkins 
      look very healthy now. I assume you have some sort of... magic that you 
      received from your Shantel, no?*</p>
    <p>Pyyon nodded, and picked up her branch from the ground, *I use my Branch 
      of Plenty to help people with plants.*</p>
    <p>The male nodded along with Pyyon, *Oh, I have power with plants as well,* 
      he replied, motioning to the two bracelets he wore on his front legs.</p>
    <p>Pyyon admired them, they were very beautiful and held two green emeralds 
      in each of the bracelets. *What do they do?* she questioned.</p>
    <p>*They are called &quot;Walder Bracelets&quot;, they helped me create that 
      energy you saw yesterday.*</p>
    <p>Pyyon blushed, *I had hoped you didn't see me.*</p>
    <p>*Ah,* the male replied, *I'm really not very scary, except yesterday.*</p>
    <p>*Those people didn't seem like they harmed anything until you started to 
      scare them off...* Pyyon said softly.</p>
    <p>The Terran looked hurt, *See, those people didn't know what they were doing. 
      All I could hear were the screams coming from the Pumpkins as they were 
      torn away from their vine.*</p>
    <p>*You hear plants?* Pyyon sounded amazed.</p>
    <p>*Another part of my powers.*</p>
    <p>*Ah... yes...*</p>
    <p>*The pumpkins here appreciate what you've done,* he nodded, *they also 
      think that I should teach you more about the care of plants and fruit. You're 
      good, but there are a few things to teach you.*</p>
    <p>Pyyon was pleasantly surprised. *Thank you... ummm...*</p>
    <p>*Moschata,* he replied, *and you?*</p>
    <p>*Pyyon.*</p>
