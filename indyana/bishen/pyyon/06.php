    <h1>Growing</h1>
    <p>Archun, Bright and Pyyon stayed in Eldora for a long time after the Hatching. 
      Pyyon did grow quite quick, unprecedented by anything Bright had known about 
      Rhul animal growth.</p>
    <p>Everyday Archun and bright would plant new things and everyday more would 
      die. The Taeds were taught how to take care of certain things, but unfortunately 
      they were just as careless as the Seques.</p>
    <p>Archun got use to the idea of taking care of another living thing. of course, 
      it was a new thing for him, at least he had Bright to guide him along the 
      way. She, in turn, didn't feel as lonely watching Archun take care of the 
      little being. She thought it was good for him too, even if he needed a lot 
      of guiding.</p>
    <p>Pyyon was a picky eater. She only enjoyed the fruits that grew up in the 
      mountains on vines near the place her egg hatched. Her favorite consisted 
      of glittery fruit that came in many colors.</p>
    <p><img src="stages/pyyond.gif" alt="Pyyon" width="203" height="322" align="right">&quot;Pyyon! 
      You're going to get chubby with all this sweet you're eating!&quot; Bright 
      would tell her from day to day. All that Pyyon would reply with was a burp 
      or a sly grin.</p>
    <p>Bright had heard from natives that a dark force had been taking over the 
      area. Most of the Taeds were nervous being outside during the day. Many 
      blamed the dark clouds on the gods who were punishing them.</p>
    <p>Archun muttered under his breath one evening, &quot;Their whole world is 
      falling apart and they blame it on a non-existing being.&quot;</p>
    <p>&quot;What did you say, Archun?&quot; Bright questioned.</p>
    <p>&quot;Oh, nothing important, I just want to move on this project as fast 
      as we can so we can head back to Hearth.&quot;</p>
    <p>Bright smiled and bent her head back down into her work, &quot;I am beginning 
      to lose hope, you see.&quot;</p>
    <p>Archun's ears perked, he had never heard her speak like this before, &quot;Heh, 
      you lose hope?&quot;</p>
    <p>&quot;Yes, me lose hope, novel isn't it?&quot;</p>
    <p>&quot;Well, it sure is different.&quot;</p>
    <p>Pyyon wandered around the landscape. She had spoken before but she was 
      quite the quiet one. She enjoyed mostly sitting by herself in one of the 
      growing trees and hanging from branches.</p>
    <p>*Archun?* she asked one day.</p>
    <p>&quot;Yes, Pyyon?&quot;</p>
    <p>*What is over there?* she pointed to the horizon, *Where does the sun go 
      when it falls asleep?*</p>
    <p>&quot;Uh... heh,&quot; Archun raised his eyebrows, unable to think of an 
      imaginative answer to tell her. &quot;Well, Pyyon, the sun never really 
      sleeps. He continues to circle our world in search of something, returning 
      each day to look over his lands once again, in hopes of finally claiming 
      his lost treasure.&quot;</p>
    <p>*Oh*, her head perked up. *Can we help him find it?*</p>
    <p>&quot;Heh,&quot; he replied, &quot;I don't think he will let us help. He 
      is forever separated from the night. I think sometimes he searches for what 
      he cannot achieve.&quot;</p>
    <p>*Does he search for the moons?*</p>
    <p>&quot;Yeah, Pyyon, he searches for the moons.&quot;</p>
    <p>*Tell me the story of the moons again, Archun! I love that one...*</p>
