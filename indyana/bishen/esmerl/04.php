      off of it was not bad either. And feel sorry for what she did? Never!</p>
    <p>It started about three years ago whenever she was in middle school. Her 
      father was out of town on business and she was expected to run the market 
      place by herself. There were a few other workers around that would be able 
      to help her should a situation arise.</p>
    <p>This was when Mr. Rahim walked into the store. Fallen had look particularly 
      glum that day.</p>
    <p>&#8220;Good morning, sir.&#8221;</p>
    <p>&#8220;Ah, Jeremy Rahim. Please to meet you young woman.&#8221;</p>
    <p>Fallen blushed, Jeremy was very attractive and to receive a compliment 
      such as &#8220;young woman&#8221; melted her heart, or instead, caused little 
      butterflies to fly around inside of her. &#8220;Good to meet you as well.&#8221; 
      At this point of her life, she was a reserved young lady and knew her place. 
      &#8220;What can I get you today, Jeremy?&#8221;</p>
    <p>&#8220;Ah, I am looking for some produce as well as some of your freshest 
      meat for dinner tonight.&#8221;</p>
    <p>&#8220;Well, you are in luck,&#8221; she smiled, Man, he was cute! She 
      walked over to the produce shelves that were still moist from the mist that 
      rained down on them. &#8220;We offer many different kinds of vegetables 
      here. Anything in particular?&#8221;</p>
    <p>&#8220;My favorites are tomatoes, and corn, of course,&#8221; he smiled 
      back at her with a sweet grin. Such a grin that made Fallen feel like she 
      was being treated just like a real lady, not the little girl she desperately 
      wanted to forget.</p>
    <p>She picked up a few tomatoes and held them out to Jeremy. &#8220;These 
      look nice and ripe. They should be perfect to make a sauce.&#8221;</p>
    <p>&#8220;I agree, excellent choice.&#8221; He grabbed the pair and brought 
      them to the front of the store. &#8220;I&#8217;ll take them!&#8221;</p>
    <p>Fallen giggled, Jeremy was acting so silly as he grinned and juggled the 
      tomatoes in his hands.</p>
    <p>&#8220;Why there we go. I knew there was a smile somewhere that could pop 
      on your face!&#8221;</p>
    <p>Fallen stopped giggling and looked up to Jeremy. &#8220;What do you mean?&#8221;</p>
    <p>&#8220;When I walked in here, your face looked so sad.&#8221;</p>
    <p>Fallen nodded but stayed quiet.</p>
    <p>&#8220;What&#8217;s your name?&#8221;</p>
    <p>&#8220;Fallen.&#8221;</p>
    <p>&#8220;Ah, what a pretty name for a pretty girl.&#8221;</p>
    <p>She smiled again, but with a worry in her brow.</p>
    <p>&#8220;Why are you so sad, Fallen?&#8221; Jeremy leaned close to her and 
      gave her a sympathetic nudge.</p>
    <p>&#8220;Well&#8230;&#8221; she paused.<br>
      &#8220;Don&#8217;t worry, go ahead.&#8221;</p>
    <p>&#8220;My father is out of town. I have to live in my large house all by 
      myself. It is scary and I don&#8217;t like being there, even if someone 
      else was there to be with.&#8221;</p>
    <p>&#8220;When will he be back?&#8221;</p>
    <p>&#8220;He&#8217;ll be back on Saturday. But until then, I have to come 
      to work every day and then go home to an empty house every night.&#8221;</p>
    <p>&#8220;Don&#8217;t you have any family nearby?&#8221;</p>
    <p>She shook her head rigidly and looked at the ground. &#8220;No,&#8221; 
      she said tight-lipped. &#8220;No one is around here. Whenever I was a little 
      kid, my parents move away from their families and move to this town.&#8221;</p>
    <p>&#8220;Ah.&#8221;</p>
    <p>&#8220;Yeah&#8230; I think I would feel better if there was someone I knew 
      nearby. But I don&#8217;t know any of my neighbors anymore. I don&#8217;t 
      do much other than work here, go to school sometimes and stay at home with 
      my father.&#8221;</p>
    <p>&#8220;Is that so?&#8221;</p>
    <p>Fallen nodded silently.</p>
    <p>&#8220;Well then, I have a solution for you!&#8221;</p>
    <p>Fallen looked up with intent eyes, &#8220;What ever could it be?&#8221;</p>
    <p>&#8220;Why don&#8217;t you come over to my house after work? I&#8217;ll 
      cook you a nice warm dinner.&#8221;</p>
    <p>Fallen smiled but held her tension in her shoulders. &#8220;That&#8217;s 
      a very nice offer, sir but-&#8220;</p>
    <p>&#8220;No but, don&#8217;t worry about it, it is no trouble for me.&#8221;</p>
    <p>&#8220;Really?&#8221; she hung on to his every word.</p>
    <p>&#8220;Really.&#8221; He wrote down his address on a piece of paper and 
      gave it to Fallen. &#8220;When can I expect you?&#8221;</p>
    <p>&#8220;Um,&#8221; she looked down at the address and estimated her time, 
      &#8220;Probably around seven. I&#8217;ll be finished here and come straight 
      over.&#8221;</p>
    <p>&#8220;Sounds good!&#8221; Jeremy paid for his items and departed with 
      a wave. &#8220;See you at seven then!&#8221;</p>
    <p>For the rest of the afternoon, Fallen drew little heart around the address 
      and daydreamed. What a lovely evening. I get to go out! Her heart leaped 
      inside her chest with every breath and she smiled from ear to ear.</p>
    <p>Finally, six-thirty rolled around and she quickly swept the store&#8217;s 
      floor and said good bye to the other employees out back. With a &#8216;click&#8217; 
      the front door was locked and she ventured out into the evening.</p>
    <p>She walked down the sidewalks of the affluent neighborhood and finally 
      reached 664 Wood Lane. It was a pleasant little two-story home with black 
      shingles on the roof and white paint covered the entire house. She walked 
      to the door and rang the doorbell. Footsteps came to the front door. The 
      hinges creaked and Jeremy poked his head through.</p>
    <p>&#8220;So nice to see you Fallen! Dinner is just about ready. Why don&#8217;t 
      you come in and sit at the table.&#8221;</p>
    <p>Fallen did just so. The wonderful smell that came from the kitchen filled 
      the entire house and made her feel welcomed and comfortable.</p>
    <p>Jeremy walked into the dining room with a plate full of pasta and another 
      with two very large steaks and places them on the table. &#8220;I hope you&#8217;re 
      hungry, I made a lot of food.&#8221;</p>
    <p>&#8220;Were you expecting more guests tonight?&#8221;</p>
    <p>Jeremy shook his head with a little smile, &#8220;Nope, just you and me.&#8221; 
      He walked back into the kitchen and returned with two glasses of milk.</p>
    <p>&#8220;This is all very nice, Jeremy. Thank you very much, it makes me 
      very&#8230; happy,&#8221; she smiled and stared at the lovely food in front 
      of her.</p>
    <p>&#8220;It was nothing.&#8221; Jeremy sat down next to Fallen and gently 
      patted her knee.</p>
    <p>Fallen&#8217;s bare legs were just short enough not to reach the floor 
      so they dangled and she swayed them back and forth as Jeremy served her 
      food.</p>
    <p>&#8220;So, where is your mother during this time? Did she go with your 
      father?&#8221; Jeremy cut himself a large piece of meat and stuck it in 
      his mouth.</p>
    <p>Fallen&#8217;s face frowned and she twirled some of the pasta on her fork. 
      &#8220;She&#8217;s&#8230; she&#8217;s dead.&#8221;</p>
    <p>Jeremy put down his fork and swallowed. &#8220;I&#8217;m so sorry, Fallen. 
      Is this recent?&#8221; he grabbed hold of Fallen&#8217;s hand and gently 
      squeezed it.</p>
    <p>She shook her head, &#8220;No,&#8221; she replied quietly, &#8220;this 
      was a while ago. But&#8230;&#8221; she looked to Jeremy and shrugged, &#8220;The 
      emptiness is still there. My father doesn&#8217;t seem to care much about 
      her anymore.&#8221;</p>
    <p>Jeremy went back to eating, &#8220;That can&#8217;t be so, I am sure he 
      is still very in love with her even if he may not show it.&#8221;</p>
    <p>&#8220;No,&#8221; she whimpered slightly, &#8220;I don&#8217;t think he 
      wants to remember her anymore. He looks at me like I am her and just gets 
      angry. Like, he is so angry at her he takes it out on me.&#8221;</p>
    <p>Jeremy chewed and swallowed, &#8220;Has he ever hurt you?&#8221;</p>
    <p>&#8220;Well, not physically. But he has hurt my feelings more than once,&#8221; 
      she finally stuck a small amount of pasta in her mouth.</p>
    <p>&#8220;That&#8217;s no way a father should treat his daughter. Don&#8217;t 
      you agree?&#8221;</p>
    <p>Fallen nodded and looked sullen.</p>
    <p>&#8220;Oh, I don&#8217;t mean to upset you Fallen. Let&#8217;s talk about 
      your mother, unless that is too hurtful for you?&#8221;</p>
    <p>Fallen shook her head no, &#8220;No, it isn&#8217;t too hurtful because 
      really, I don&#8217;t remember much about her. I remember nice things though. 
      It helps me imagine a mother that really loved me and hopefully, still watching 
      over me.&#8221;</p>
    <p>&#8220;That&#8217;s a very good idea, Fallen. You know, for a young lady 
      such as yourself, you sure have a very adult way of looking at things.&#8221;</p>
    <p>&#8220;Really?&#8221; she smiled and ate more.</p>
    <p>They continued to talk for a few more hours, long after the food had disappeared 
      from their plates and many laughs had been shared.</p>
    <p>&#8220;Oh my, it is getting late, Fallen. You should probably head home 
      so that you get a good night sleep before work tomorrow.&#8221;</p>
    <p>Fallen frowned, &#8220;Aww, it is late, isn&#8217;t it?&#8221; She got 
      up from the table and stood in front of Jeremy &#8220;Thank you again.&#8221; 
      She wrapped her arms around the man and gave him a strong hug.</p>
    <p>&#8220;You&#8217;re welcome, Fallen,&#8221; he replied as he returned the 
      hug.</p>
    <p>The two walked to the front door and Jeremy opened it for the young lady. 
      &#8220;When was it that your father was going to be back?&#8221;</p>
    <p>&#8220;Oh, not until Saturday.&#8221;</p>
    <p>&#8220;Well, I had a lovely evening tonight, how about you come over tomorrow 
      night as well and we&#8217;ll do this again?&#8221;</p>
    <p>Fallen flashed a smile but calmed herself down quickly, didn&#8217;t want 
      to sound too much like a little girl, especially since Jeremy had complimented 
      her on being adult. &#8220;Of course, it would be absolutely lovely!&#8221;</p>
    <p>The next day at work, Fallen spent the day in reverie, remembering her 
      evening to Jeremy. I think I am falling in love, she would whisper to herself 
      from time to time.</p>
    <p>Across town, Jeremy wrestled with his own actions&#8230;</p>
    <p>The evening came and once again Jeremy had prepared a lovely meal and once 
      again the pair sat down to enjoy it together.</p>
    <p>This night, the topic of conversation was about her schooling.</p>
    <p>&#8220;So, you don&#8217;t go to school anymore?&#8221; Jeremy asked through 
      a mouthful of bread.</p>
    <p>Fallen shook her head, &#8220;Nah, I don&#8217;t need it anymore.&#8221; 
      She chewed and swallowed, &#8220;I work for my dad mainly and sometimes 
      attend classes when I feel like it, like if I am not needed at the store.&#8221;</p>
    <p>&#8220;Ah,&#8221; Jeremy nodded his head and continued to eat.</p>
    <p>&#8220;I mean really, why do you need to go to class whenever you have 
      books that&#8217;ll teach you the same thing?&#8221;</p>
    <p>&#8220;Exactly,&#8221; Jeremy continued to nod his head. &#8220;But don&#8217;t 
      you have friends at school that you&#8217;d like to spend some time with?&#8221;</p>
    <p>Fallen frowned, &#8220;No, they don&#8217;t like me there&#8230;&#8221;</p>
    <p>&#8220;Really?&#8221; he looked astonished. &#8220;Why would such a nice 
      lady like you not have friends just dying to spend time with her?&#8221;</p>
    <p>Fallen blushed, &#8220;I am just too quiet I guess, also the fact that 
      my father didn&#8217;t really let me go out all that often. He always told 
      me that I should be doing work or something productive.&#8221;</p>
    <p>&#8220;Wow, well I feel honored that you&#8217;ve come to spend the evening 
      with me.&#8221;</p>
    <p>&#8220;Oh, I am honored as well for having such a wonderful host.&#8221;</p>
    <p>These nights of enjoyment continued until Friday night, whenever Fallen 
      knew her father would be coming home the next day.</p>
    <p>&#8220;I guess this means that our time together is over,&#8221; Fallen 
      frowned, feeling quite heartbroken.</p>
    <p>&#8220;Aww, no, Fallen. I can still come visit you at work. Maybe we could 
      spend evenings together sometimes.&#8221;</p>
    <p>&#8220;I&#8230; I don&#8217;t know. I don&#8217;t think my father would 
      like it.&#8221;</p>
    <p>&#8220;Well,&#8221; Jeremy paused, &#8220;Your father doesn&#8217;t have 
      to know.&#8221;</p>
    <p>Fallen gave Jeremy a perplexed look. &#8220;Not tell him? But how would 
      that work?&#8221;</p>
    <p>Jeremy shrugged &#8220;You could say you&#8217;re visiting friends, that&#8217;s 
      not a lie.&#8221;</p>
    <p>Fallen nodded slowly, &#8220;Yeah&#8230; I could plead with him and complain 
      because he never let me out as a kid. If I promise to be home by sun down, 
      I don&#8217;t see why not.&#8221; She grinned from ear to ear.</p>
    <p>&#8220;Great thinking!&#8221; Jeremy said as she patted her shoulder.</p>
    <p>&#8220;Of course, I doubt it would be all the time, maybe once or twice 
      a week. He is usually at the store until sun down anyway.&#8221; She was 
      pleased with herself for thinking up such a wonderful plan.</p>
    <p>&#8220;Well then,&#8221; Jeremy knelt down in front of Fallen, &#8220;I&#8217;ll 
      see you&#8230;?&#8221;</p>
    <p>&#8220;How about Tuesday?&#8221;</p>
    <p>&#8220;Tuesday it is.&#8221; He gave her a hug and she hugged him back 
      and before he let go of the embrace, he gave her a soft kiss on her forehead.</p>
    <p>Fallen smiled politely as Jeremy opened the door and she walked outside 
      and back to her home.</p>
    <p>It was a few agonizing days waiting until she could see Jeremy again. She 
      anticipated their next &#8220;date&#8221; would be better than the last. 
      And that kiss! That kiss meant the world to her. She hadn&#8217;t felt that 
      extraordinary since&#8230; well in her whole life. Maybe he would be her 
      boyfriend. She dreamed the days away and toiled in the store until the clock 
      clicked to 6 o&#8217;clock, the time her father usually let her off while 
      he was around, and would head home. When Harold returned home on Monday 
      night, she asked him sweetly, &#8220;How was your trip, daddy?&#8221;</p>
    <p>Harold walked in and shut the door without saying a word.</p>
    <p>Fallen stood in her place and continued to smile although her eyes said 
      something different.</p>
    <p>&#8220;It&#8217;s been two days, Fallen. And now you&#8217;re asking me 
      how my trip was?&#8221;</p>
    <p>Fallen grimaced and replied, &#8220;Well, we haven&#8217;t had a chance 
      to sit down and talk, I thought we could now.&#8221;</p>
    <p>&#8220;Ah, don&#8217;t start this Fallen. What is it that you want?&#8221;</p>
    <p>Fallen rolled her eyes and loosened her broomstick stance. &#8220;Nothin&#8217;, 
      Dad.&#8221;</p>
    <p>She turned and headed up the stair case to her room. He would never understand, 
      he would never listen, and he would never let me go.</p>
    <p>She shut the door quietly and slide down to the floor beside it. She silently 
      allowed a few tears drop down her cheek but stopped, thinking it unladylike 
      to continue.</p>
    <p>It was then she imagined Jeremy, smiling at her. The face of confidence 
      in her and the comfort she never received from her father. He was out her 
      window on a bright white horse as a crisp autumn wind ran through the trees, 
      beckoning her to climb down from her second story window to join him below.</p>
    <p>That&#8217;s it! She thought, I can climb out my window whenever my father 
      goes to bed. He won&#8217;t know a thing.</p>
    <p>She twiddled away the next day, nervous about her plan; maybe her father 
      had sharp ears. But then again, he snored like a beast at night, if he couldn&#8217;t 
      wake up form something like that, how could he hear her faint feet upon 
      the siding on the other end of the house?</p>
    <p>And then the night finally came. Fallen was in her bedroom behind her door 
      and listened for the footsteps of her father as he climbed the staircase.</p>
    <p>&#8220;Good night, Fallen!&#8221; he hollered down the hall and then shut 
      the door. She would wait longer until he started snoring. She could hear 
      his fumbled footsteps as he undressed and then lazily got into bed, murmuring 
      all the while in an indistinct tone.</p>
    <p>Within fifteen minutes, he was fast asleep and snoring loud as ever.</p>
    <p>She quickly unhitched her window and took a look down the siding. It would 
      be a challenge, but she thought she could do it. Anything to get out of 
      here. Luckily, there was a vine rack outside her window which lent help 
      to guide her feet down the side. One step, two steps, one step, two steps 
      her feet moved. She took her time and eventually she made it to the bottom 
      with a few cuts and scratches. Her poor little blue dress was also dinged 
      up, but it didn&#8217;t matter. She didn&#8217;t need to look perfect where 
      she was going.</p>
    <p>She knocked on his door in her tattered clothes. Footsteps approached the 
      door and the young girl was already watering the ground with her tears. 
      Without a word, Jeremy scooped her in his arms and carried her inside.</p>
    <p>It was like a scene from a trashy novel, written by an old woman past her 
      prime who yearned for her own seductive affair, with of course, a happy 
      ending.<br>
