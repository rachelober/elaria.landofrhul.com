    <h1>Chapter 4</h1>
    <p>Now, Fallen hadn&#8217;t always been a &#8220;prostitute&#8221;. In fact, 
      she hated that word because she thought of her adventures as good for her 
      as well as for the men she treated. It was nice for them because of the 
      obvious benefits, but for her, it gave her a chance to meet men out there 
