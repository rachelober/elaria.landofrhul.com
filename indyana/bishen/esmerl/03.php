    <h1>Chapter 3</h1>
    <p>It was a Saturday morning, it was nice and warm out, as it usually was 
      in this part of the land, but Fallen was inside working the cash register. 
      Her father, Harold, was outside attempting to sell some plants or trees, 
      or whatever, to a customer. Her father would almost offer the world just 
      to make a sale. What a silly man, she thought, would do anything to sell 
      his soul but nothing to praise his own daughter. She scowled at the man 
      she saw through the window.</p>
    <p>The bell to the store door chimed and her disgust quickly turned to a joyful 
      expression on her face. &#8220;Why hello, welcome to Harold&#8217;s Market. 
      How can I help you today?&#8221; The sweetness sickened her.</p>
    <p>The man that walked in was tall and cut very well, as if from the strongest 
      stone. He wore a denim jacket with denim pants and very nice boots that 
      had steel on the tips. He made her look like a pile of rags in her plain 
      blue dress and white apron.</p>
    <p>&#8220;Umm&#8230; yes. I was wondering when your shipment of western fish 
      comes in the morning.&#8221;</p>
    <p>&#8220;Ahh, looking for a specific fish?&#8221; she raised an eyebrow and 
      tilted her head slightly.</p>
    <p>He approached the desk slowly and spoke cautiously, &#8220;well, sort of. 
      I&#8217;d like to have fresh fish rather than wait for later in the day 
      or perhaps even have them from a previous day.&#8221;</p>
    <p>&#8220;Well then. I would suggest getting over here at 7am. That&#8217;s 
      when they are loaded off the truck and we put it on the shelves you see 
      in here.&#8221; She leaned forward and put her elbows on the counter. &#8220;I&#8217;m 
      here bright and early working, so you&#8217;re bound to get a good deal 
      then.&#8221;</p>
    <p>The man smirked, &#8220;Ah, I see. I will come back tomorrow to get my 
      deal then.&#8221; He nodded his head to her and turned to leave.</p>
    <p>&#8220;He was cute,&#8221; she said to herself, &#8220;definitely cute. 
      I do hope he comes by tomorrow.&#8221;</p>
    <p>&#8220;FALLEN!&#8221; a yell came from the back of the store. Her father 
      emerged, raged, and carrying dead rats in his hands, yes- dead rats in his 
      hands. &#8220;I told you to call the exterminator last week!&#8221;</p>
    <p>&#8220;I did! Eeewww, get those away from me!&#8221; she ran away and stood 
      by the door.</p>
    <p>Harold followed her, shaking his full fists wildly, &#8220;you were in 
      charge of getting them over here. I put the full responsibility on you to 
      get this done. I put you responsible in cleaning the god-damn mess that 
      is now in the supply room. Do you hear me, girl? Right this minute and I 
      don&#8217;t want you to come out of there until every last filthy rat is 
      picked up off the ground and the dropping mopped up!&#8221; he stormed out 
      of the market door and raged outside for a little until he threw the rats 
      into a dumpster outside.</p>
    <p>Fallen&#8217;s heart was racing as she quietly stepped back from the door, 
      still facing the window watching that her father was not coming back inside 
      for another round, then quickly turned to run to the supply room.</p>
    <p>When she got there, it stank from the many death of unfortunate little 
      beast and the little presents they left behind.</p>
    <p>She hated rats.</p>
    <p>Absolutely hated the way they twitched their nose at her and then especially 
      their own putrid hisses whenever she found them in the food, as if they 
      were complaining that the food they were selling was not up to taste.</p>
    <p>But she hated her father more- especially when he ordered her to do such 
      work.</p>
    <p>She put on some gloves and quickly weeding through the boxes on the shelves, 
      finding a few dead rats here plus the occasional living rat that would hiss 
      at her and try to protect his food. Fallen envisioned taking a riffle and 
      shooting each and every last rat on the face of the planet, and then turning 
      the gun on herself. She shook her head and sighed, looking up to the ceiling 
      of the small supply room. A lonely light bulb hung from a thin string and 
      waved back and forth.</p>
