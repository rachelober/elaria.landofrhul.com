    <h1>Chapter 5</h1>
    <p>That was Jeremy and that was then, now she was three years older and wiser. 
      For two years she had secretly visited Jeremy by climbing out her window 
      a few nights a week to spend the darkness wrapped in sheets with the man 
      she loved.</p>
    <p>He was at least 15 years her senior. In Yaluno at that time, no statutory 
      rape laws were ever enforced, well even created for that matter, but it 
      was considered bad taste to date someone that young.</p>
    <p>As it turned out, one evening as she was leaving her bedroom window, Fallen 
      had fell from her wall and broke her arm. Her father had heard her that 
      evening as she cried, unable to move in the dew dropped grass and had taken 
      her to the emergency ward at the nearby hospital.</p>
    <p>By her bedside as she nursed her newly appointed arm cast, he stared her 
      down. &#8220;And just what, may I even dare to ask, were you doing hanging 
      from the second storey window, anyway?&#8221;</p>
    <p>How could she defy him this time? She looked down at her arm for a minute 
      and then brought her eyes to his. &#8220;I am in love.&#8221;</p>
    <p>He looked skeptical. &#8220;With what exactly? The grass?&#8221;</p>
    <p>She rolled her eyes, she might as well say everything. She was a grown 
      woman now, just like Jeremy had told her. &#8220;With a man of course.&#8221;</p>
    <p>Harold&#8217;s eyes narrowed, &#8220;Does he know about this little fantasy 
      of yours?&#8221;</p>
    <p>Fallen spitted back, &#8220;He was the one that initiated it. And get this. 
      He loves me too.&#8221;</p>
    <p>Harold stopped for a moment. &#8220;Is this an older man? Is this why you 
      had to sneak out?&#8221;</p>
    <p>Fallen relaxed a bit, &#8220;Why, yes. A wonderful man.&#8221;</p>
    <p>&#8220;Do I know this &#8216;man&#8217;?&#8221;</p>
    <p>&#8220;Jeremy Rahim.&#8221;</p>
    <p>&#8220;He&#8217;s at least a decade older than you. I thought maybe you&#8217;d 
      just be going for someone a few years older than you. But no, you had to 
      disgrace your father by hiring yourself out to man!&#8221; He was getting 
      increasingly irate by this point; the main indication was the bright red 
      tendency his checks tended to get when he was upset.</p>
    <p>&#8220;I am NOT hiring myself out!&#8221;</p>
    <p>&#8220;I don&#8217;t know why else he would want to be with you, Fallen. 
      A man his age, Fallen. He isn&#8217;t looking for a committed relationship 
      with a thirteen year old girl. He is looking for some fast ass if you understand 
      what I am saying!&#8221;</p>
    <p>Tears began to well up in Fallen&#8217;s eyes. No, not now, Fallen, you 
      mustn&#8217;t let yourself to fall.</p>
    <p>A knock came to the bedroom room. The doctor stood in the door with a skeptical 
      eyebrow and a clipboard in her left hand. &#8220;Um, yes is everything alright 
      in here?&#8221; She continued to walk into the room and cross her arms.</p>
    <p>Harold spoke and Fallen tried to wipe away her childlike essence from her 
      face. &#8220;Uh, yes, I was just discussing the implications of my daughter&#8217;s 
      choices which led to her unfortunate accident last night.&#8221;</p>
    <p>&#8220;Ah,&#8221; she nodded her head, &#8220;Mr. Banmira, I implore you 
      to assert some kind of self control in this facility. Your tone will not 
      help Fallen achieve the sort of recovery she needs in order to heal.&#8221;</p>
    <p>Harold nodded his head, &#8220;Well, I assume you are here to check up 
      on her, I will wait in the other room.&#8221;</p>
    <p>The doctor nodded and watched him as he grabbed his jacket and sauntered 
      out of the room.</p>
    <p>&#8220;Now then,&#8221; she uncrossed her arms and took the seat next to 
      Fallen&#8217;s bed. &#8220;How are you feel after some rest? I was concerned 
      that you might have bruised some ribs after your fall.&#8221;</p>
    <p>&#8220;Well,&#8221; Fallen started, &#8220;It has been a little&#8230; 
      laboring to breathe since waking.&#8221;</p>
    <p>&#8220;Uh huh,&#8221; the doctor affirmed as she grabbed the clipboard 
      and began to write notes.</p>
    <p>&#8220;My back hurts a little, but I think I can move fine. And then there 
      is the obvious, my arm. That hurts but we&#8217;ve already taken care of 
      it.&#8221;</p>
    <p>&#8220;I expect your cast will be on for a few months. Now, you&#8217;ve 
      received information about how to care for it, right?&#8221;</p>
    <p>Fallen nodded her head silently.</p>
    <p>&#8220;Good. Now, onto more important things since I believe, in my medical 
      opinion, you will make a full recovery.&#8221;</p>
    <p>Fallen smiled slightly, the doctor made her feel safe in this cold and 
      lonely building.</p>
    <p>&#8220;Now, I need to make sure that more things aren&#8217;t going on 
      here. By the way your father talked to you I was worried that there may 
      be some sort of domestic violence going on in your household. I know your 
      mother has&#8230; passed away. Has this caused tension between you and your 
      father?&#8221;</p>
    <p>Ah, the mother question. She has now conditioned herself so many times 
      to answer this in an appropriate matter but this was different. Maybe she 
      should tell her everything&#8230;</p>
    <p>&#8220;Yes, yes it has quite&#8230; made a mark on our relationship.&#8221;</p>
    <p>&#8220;How so?&#8221;</p>
    <p>&#8220;Well, I think he doesn&#8217;t want me around anymore.&#8221;</p>
    <p>&#8220;When was it that your mother died?&#8221;</p>
    <p>&#8220;Um, I was six, so wow, seven years ago.&#8221;</p>
    <p>&#8220;You said wow, what do you mean?&#8221;</p>
    <p>&#8220;Wow, as in I can&#8217;t believe it has only been seven years.&#8221;</p>
    <p>&#8220;Ah, only is the key word here. Have you felt that it should be more 
      than seven years?&#8221;</p>
    <p>Fallen nodded, &#8220;It seems this&#8230; terrible relationship between 
      my father and me has lasted my entire life. Although I know it only started 
      after my mother died. He was quiet after her death and wouldn&#8217;t speak 
      to me. I had to rely on neighbors since my family did not stick around long 
      after the funeral. So, I haven&#8217;t been much of a family person after 
      that. Seems like they don&#8217;t do much good after all.&#8221;</p>
    <p>The doctor leaned in and put the clipboard on her lap. &#8220;This is interesting, 
      Fallen. But I worry that your living condition isn&#8217;t right for you.&#8221;</p>
    <p>&#8220;I would agree with you, ma&#8217;am. I don&#8217;t even go to school 
      on a regular basis.&#8221;</p>
    <p>Now, there were no laws governing that children were required to go to 
      school, but remember Fallen was currently talking to an empowered woman 
      who had worked her way through the trenches of a prestigious college to 
      get where she was in pediatric medicine. &#8220;Well, we can&#8217;t have 
      that now, can we?&#8221;</p>
    <p>Fallen shook her head. Not that she really wanted to go to school, but 
      it would be nice if she didn&#8217;t have to work at the store anymore.</p>
    <p>&#8220;Well, I will see what I can do.&#8221; The doctor patted her hand 
      and squeezed it lightly with a smile. The warmness of her face made Fallen 
      miss her mother this time the most when she told her story.</p>
    <p>Unfortunately, the doctor probably concentrated on the wrong aspect of 
      Fallen&#8217;s situation. When she cared about Harold&#8217;s abuse, she 
      failed to realize Fallen&#8217;s own mishap with Jeremy.</p>
    <p>Jeremy never visited Fallen when she stayed at the hospital those few days. 
      But Fallen forgave him for that, he was probably smart enough to realize 
      that Harold would be upset if an older man came to visit his daughter.</p>
    <p>But, it was when she got out of the hospital and walked over to his house, 
      she could no longer forgive him.</p>
    <p>She walked along with a sling over her arm, carelessly jaunting about the 
      sidewalk until she came across his house. There a car was pulled up in his 
      driveway and an attractive woman opened the door on the driver&#8217;s side.</p>
    <p>Jeremy emerged from his house with a warm smile, the one he always gave 
      her, and went over to the woman, giving her an embrace and kissing her full 
      on the lips. The two then went to the passenger side of the car and the 
      woman opened the door, ducked inside and surfaced with a young girl, maybe 
      two years old. The woman carried the girl to Jeremy and he received her 
      with open arms and held her tight to his chest. It was then that their eyes 
      locked, Fallen pleading to Jeremy, What was this, tell me now, and come 
      to me! And Jeremy tilting his head and then turning his body to go back 
      into his home with the woman and the girl.</p>
    <p>Fallen stood there for a few moments and watched as the door closed. A 
      cool wind blew across her face and made the tears sting in her eyes.</p>
    <p>Within a few weeks, Fallen was in school, but hated every minute of it. 
      The doctor had contacted Harold and instructed him that if she was not in 
      school she would report him to the proper authorities. Harold, unwilling 
      to tarnish his sterling reputation in the town, accepted her threat and 
      enrolled Fallen in the local private school.</p>
    <p>&#8220;Are you enjoying school, Fallen?&#8221; Harold asked one night as 
      they sat down to dinner one night, another suggestion of the doctor.</p>
    <p>Fallen stared down at her plate and answered, &#8220;No.&#8221;</p>
    <p>&#8220;No?!&#8221; Harold blurted, &#8220;I am not paying this private 
      school just for you to not like it!&#8221;</p>
    <p>Fallen rolled her eyes and pushed her mashed potatoes around with her fork.</p>
    <p>&#8220;Well, say something Fallen!&#8221;</p>
    <p>&#8220;Something.&#8221; She sarcastically remarked.</p>
    <p>Harold threw down his napkin on the table, &#8220;If you aren&#8217;t trying 
      to like the school, or didn&#8217;t even want to go there in the first place, 
      why am I sending you there?&#8221;</p>
    <p>&#8220;Because I wanted to get away from YOU!&#8221; she shouted.</p>
    <p>Harold narrowed his eyes.</p>
    <p>&#8220;If it wasn&#8217;t for the fact that I didn&#8217;t have to see 
      you every day and talk to you, I would still be working at the store! And 
      I think you know that!&#8221;</p>
    <p>&#8220;Then stop wasting my money, girl!&#8221;</p>
    <p>Fallen laughed, &#8220;I rather see you taken down town, mister!&#8221; 
      Harold stood up quick and raised a hand and Fallen covered her face. Harold 
      stopped and sat down once again. Peeking from behind her hands, Fallen continued, 
      &#8220;You know how quickly I would report you to that doctor. She&#8217;d 
      wrap you in cuffs so quickly, and then you&#8217;d lose your entire business. 
      Who would want to go to a child abuser&#8217;s store, hmm?&#8221;</p>
    <p>Harold was strangely quiet, so Fallen carried on.</p>
    <p>Fallen smirked, &#8220;I have a proposition for you, I&#8217;ll drop out 
      of school again- we won&#8217;t tell that woman, and I&#8217;ll come back 
      to the store. BUT you let me do whatever I want with my time outside of 
      the store. No questions asked.&#8221;</p>
    <p>Harold&#8217;s mouth tightened, Fallen seemed to have the upper hand in 
      this conversation. Either way, his little girl would find someway to ruin 
      his reputation, it was just how badly he would let it happen.</p>
    <p>&#8220;I swear to you, child, if you so much as make me look bad in front 
      of my patrons I&#8217;ll-&#8221;</p>
    <p>&#8220;Nah, anything I do will be secret,&#8221; she smiled deviously.</p>
    <p>Harold swiveled his head skeptically. &#8220;Against my better judgment, 
      I will hold you at your word. If you so much as go back on it, I will send 
      you to boarding school so quick, and no matter the cost!&#8221;</p>
    <p>Fallen stuck forward her hand, &#8220;Then let&#8217;s shake on it, father.&#8221;</p>
    <p>Harold paused for a moment, then casually offered his own hand, &#8220;On 
      your word, daughter.&#8221;</p>
    <p>And that is how it was today. Fallen would work in the store from 9-5 and 
      afterwards that time was hers for her own adventures. As much as her father 
      disliked her male company, Fallen was good at keeping it discreet, especially 
      since most of her male friends wanted to keep their affairs secret as well.</p>
    <p>And what happened to Jeremy, you might ask?</p>
    <p>Well, as it just so happened, a few weeks after Fallen and Harold&#8217;s 
      duel, he waltzed into the store when it was just Fallen at the desk, much 
      the same way two years ago.</p>
    <p>&#8220;Why Fallen,&#8221; Jeremy entered the store with the little bell 
      heralding his arrival, &#8220;Your poor arm, how many more weeks will you 
      have that cast?&#8221;</p>
    <p>&#8220;Hmm,&#8221; she smiled, &#8220;Oh, only a few.&#8221;</p>
    <p>&#8220;I was sorry to hear about your accident.&#8221;</p>
    <p>&#8220;Oh, really now,&#8221; her expression turned dark.</p>
    <p>&#8220;Of course,&#8221; he laid his hand on her free one which was lying 
      on the counter.</p>
    <p>&#8220;You didn&#8217;t seem to care much whenever you gazed from your 
      front yard holding that little girl in your arms.&#8221;</p>
    <p>&#8220;Ah, I hoped we wouldn&#8217;t fight about this and let it be.&#8221;</p>
    <p>&#8220;Not fight about this, Jeremy!&#8221; she started to get hysterical.</p>
    <p>&#8220;Shh, shh, Fallen, no need to get all excited, now,&#8221; he said 
      as he motioned for her to keep her volume down.</p>
    <p>&#8220;Why shouldn&#8217;t I get excited?&#8221;</p>
    <p>&#8220;Because you&#8217;re acting childlike, Fallen. Please, act responsible. 
      You didn&#8217;t really think our relationship was going to last long? Heck, 
      I am surprised it lasted two years.&#8221;</p>
    <p>Fallen heart stung, and it stung hard. &#8220;Childlike.&#8221;</p>
    <p>&#8220;Yes, Fallen,&#8221; he raised his eyebrows in a condescending motion, 
      &#8220;This is not how a young lady should represent herself.&#8221;</p>
    <p>&#8220;And you believe, a man your age should be acting this way as well?&#8221;</p>
    <p>Jeremy shrugged, &#8220;You&#8217;re a cute girl, Fallen. And I like you. 
      But, my estranged wife came back to me after two years. Can you believe 
      it?&#8221;</p>
    <p>Fallen&#8217;s heart fell, &#8220;You were married?&#8221;</p>
    <p>&#8220;Yep, still am.&#8221;</p>
    <p>She sighed and looked from side to side, Why? She asked herself, Why is 
      this happening?</p>
    <p>&#8220;Look, we can still be friends, Fallen, but now that my wife is back 
      I can&#8217;t see you all the time. And my daughter,&#8221; he nodded and 
      laughed to himself, &#8220;A glorious girl, very sweet. I haven&#8217;t 
      seen her in a long time.&#8221;</p>
    <p>&#8220;A daughter?&#8221; she squeaked out. Thoughts raced in her mind 
      and she should hardly keep them straight. Here she thought she could rip 
      into Jeremy for never visiting her and here she could hardly get in word 
      edgewise as he kept cutting into her heart with every syllable emerging 
      from his lips.</p>
    <p>&#8220;Yes, her name is Abidemi, Abby for short.&#8221;</p>
    <p>She was choked up but the only thing she could think to say was, &#8220;That&#8217;s 
      a beautiful name.&#8221;</p>
    <p>He nodded again, smiling. &#8220;Thank you Fallen for regaining your composure,&#8221; 
      he winked.</p>
    <p>Inside, Fallen wanted to strangle the man, but deeper inside still, she 
      wanted to stay the woman that she knew he still loved. She nodded to him, 
      without saying another word, feeling that if she would open her mouth again 
      her feelings would come out in a sloppy mess of embarrassment.</p>
    <p>&#8220;Well, it was really nice to see you again, Fallen. But I just came 
      by to pick up some milk.&#8221; He went to the cooling chest and brought 
      back a carton and paid. With a quick wave he exited the store and all that 
      was left was the reverberation of the bell, declaring his leave.</p>
