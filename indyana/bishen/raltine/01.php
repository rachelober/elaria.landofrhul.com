    <h1>Beginning Discourage</h1>
    <p>The sun sat upon the crest of a horizon. There, a knight stood by himself, 
      still dressed in his armor and sword unsheathed, reminiscing his daily battle. 
      Mx&#8217;tiador&#8217;ol had been under siege by the heathens from Churro 
      . Again, and again they attempted to take the castle from his Lord.</p>
    <p>&#8220;Damn heathens,&#8221; he muttered under his breath, and wiped his 
      sword clean from the nearly dried blood. Without a kujiin and no sign of 
      a boat to carry him back across the river, Menshin dropped lazily to the 
      ground.</p>
    <p>His kujiin had been swept from under him during the raid and he had been 
      tossed forward, nearly smashing his head into a rock. Lucky for him, his 
      twirling body dodged the protruding rock and his back received the blow 
      from the earth.</p>
    <p>&#8220;Now where did that bloody back pack of mine get thrown to?&#8221; 
      Unable to get back up after his lazy drop, Menshin crawled along the area 
      in the dimly lit meadows, stopping intermittently to catch his breath and 
      take a more general look around. &quot;With my luck, one of them lived and 
      is ready to pounce me,&#8221; he talked of the heathen desert people.</p>
    <p>His last kill, still whispering on the tip of his breath, Menshin careened 
      to a corner where a lonely tree, undamaged by the battle on the outer most 
      barrier of the war field, sat strong in the ground where Menshin propped 
      himself up. There he slept for the night, in hopes to awaken healthier in 
      the morning.</p>
    <p>Sticky morning dew lay upon Menshin&#8217;s beard as his eyes opened clearly 
      to the battlefield in the early hours of the day. Only his dirty hands and 
      face exposed themselves to the chilled winds of the morning. He was still 
      dressed in his thick silver plated armor. The dreaded weight of the suit 
      stole space for his lungs to expand, and therefore slowly he stood, careful 
      not to sprain a joint, and stripped to his tunic.</p>
    <p>Like a chick removing its birth shell, Menshin lifted each piece of armor 
      and discarded it on the cold morning ground. After emancipating his tired 
      and worn body, Menshin rotated his creaking shoulders and cramped neck.</p>
    <p>&#8220;The sleep was not as comforting as I&#8217;d wished,&#8221; he mumbled 
      to himself, as he often did, and remembered from last night his rider's 
      pack that had fallen off when he was knocked down from his kujiin.</p>
    <p>The southern province of Mx&#8217;tiador&#8217;ol received much water from 
      the Braufoam River and the plants grew luscious in the fertile soil. The 
      area stretching from the northern Arube forest across Mx&#8217;tiador&#8217;ol 
      and the Braufoam River on its way to Emblem stayed relatively calm for most 
      part of the year except during the rainy season when the ground tended to 
      be flooded by the river. This made the land easy to plow and plant crops, 
      which promoted livelihood of communities and a superfluous amount of residents 
      in the fertile sector. </p>
    <p>Menshin&#8217;s parents had bought a small shanty outside of the main providence 
      of Mx&#8217;tiador&#8217;ol right before he was born. There he was raised 
      until the age of 6 when the Government mandated his family that any boy 
      within the providence was to take up residence in the castle and learn the 
      ways of the Knight. Personally, Menshin remembers little of his time with 
      his parents and instead creates his own figments of family life before the 
      installment of warfare in his blood. </p>
    <p>Ladies of the castle cared for the young squire until he was twelve. Days 
      were spent tending to the King&#8217;s legions of kujiin and learning from 
      the young maidens of chivalry and courtly love. During his residency at 
      the castle, he met many other squires and produced brotherhoods with dozens 
      of males waiting their knighting. </p>
    <p>Unfortunately, most of those men had fought in the recent battle and lay 
      dead on the field. The Metsheps (Meet-she-eps) from northern Churro remained 
      sturdy in their legions of militants and slowly chipped away at the foundation 
      of Mx&#8217;tiador&#8217;ol&#8217;s army. At least, this was what Menshin 
      perceived.</p>
    <p>&#8220;&#8217;Lo! Menshin!&#8221; a voice rang from a few kilometers down 
      the hill he sat upon. From his sight, Menshin believed it was a cavalryman 
      come to find him. &#8220;Menshin, good buddy, why are you still out here? 
      It has been days since you fought in this battle.&#8221;</p>
    <p>Menshin began to stand but felt the pain in his back rise again only to 
      bring him to the ground once more. &#8220;&#8217;Lo, good sir,&#8221; Menshin 
      replied, gasping a little but quickly hiding his pain from the cavalryman.</p>
    <p>&#8220;Sobeslav, cavalry,&#8221; the servant replied. &#8220;Many days 
      have you wandered, I am sure your lord has expected your return by now.&#8221;</p>
    <p>&#8220;Days, young master?&#8221; Menshin cocked his head, &#8220;The battle 
      lies only hours dead, do you understand who I am?&#8221;</p>
    <p>&#8220;Know who you are, no, but your placement, yes. You are one of the 
      only survivors of the Battle for Braufoam. That battle died three days ago, 
      mastery sir.&#8221;</p>
    <p>&#8220;Three days, servant?&#8221;</p>
    <p>&#8220;Correctly repeated, mastery sir.&#8221;</p>
    <p>&#8220;What crippled bones my body must circumscribe! Why has no one found 
      me sooner?&#8221;</p>
    <p>&#8220;Mastery sir, the lands south of Mx&#8217;tiador&#8217;ol have continued 
      to be bludgeoned by attacks, you residing on the outermost province has 
      inhibited your discovery.&#8221;</p>
    <p>&#8220;Of course, the luck of a blacksmith&#8217;s son.&#8221;</p>
    <p>The servant outstretched his hand to Menshin&#8217;s in order to help lift 
      him up from the ground. &#8220;Well, good mastery sir, if I may be so bold, 
      luck of a blacksmith&#8217;s son had nothing to do with it. You were far 
      from danger, therefore in your concussion, no invader happened upon your 
      unguarded body and slaughtered you as a butcher might a blind piglet.&#8221;</p>
    <p>The servant finally picked Menshin off the ground, wheezing under his breath, 
      obviously this man was not healthy to be in the army, and therefore Menshin 
      knew, as he assumed, his placement to be no more than a lord&#8217;s servant. 
      &#8220;Ah, of course. Instead I am left nearly incapable of withstanding 
      the blowing of the wind,&#8221; he jokingly coughed, allowing his pride 
      to whither a few notches in front of the servant, he needed more than one 
      man to sustain his fractured body.</p>
    <p>The servant held Menshin steady and reached to his right side where a horn 
      hung from a strap. Carefully, he lifted the horn&#8217;s mouthpiece to his 
      lips and blew a loud monotone note through the fields. &#8220;My buddies 
      are out searching as well, I think we&#8217;ll need more than just one of 
      us to carry you back to the castle.&#8221;</p>
    <p>&#8220;Ah, you are weak spirited, friend,&#8221; Menshin asserted, keeping 
      his pride full once again, yet secretly thanking Aille.</p>
    <p>Soon, four men rode over the hills. There were three kujiins, two pulled 
      a carriage and the third carried two men. &#8220;&#8217;Lo, Jurrah! I see 
      you&#8217;ve found another one,&#8221; one man on the kujiin carrying two 
      men called to the servant. He soon jumped off and allowed the other man 
      hold on to the reins. He swaggered to the couple and grabbed the other arm 
      of Menshin and propped it over his shoulder. &#8220;We&#8217;ll have you 
      home as soon as possible, soldier. Jurrah, just lead him towards the carriage 
      and the other men will take care of the rest.&#8221; By now the other men 
      had dismounted their kujiin and had prepared the carriage for Menshin to 
      sit in. They lowered him into his seat and Menshin cringed as they touched 
      some of his weaker bones but otherwise kept a straight face.</p>
    <p>&#8220;Ideally,&#8221; one of the other men commented, &#8220;We should 
      have you back to the castle by tomorrow. We will have to see how the conditions 
      are.&#8221; He shot a glance to the lead servant, weary of the leader&#8217;s 
      decisions, and turned back to Menshin. &#8220;Until then, you need to calm, 
      we&#8217;ll take care of the rest, don&#8217;t you worry, mastery sir.&#8221;</p>
    <p>Menshin nodded, &#8220;Well, that all depends on how you take this carriage 
      ride, take heed not to hit any divots or rocks, mind you.&#8221;</p>
    <p>&#8220;Yes, sir, mastery sir,&#8221; Jurrah bowed. He soon jumped onto 
      one of the kujiin who pulled the carriage with the lead servant and began 
      riding. Menshin lay back in the carriage and shut his eyes, drifting far 
      from his battles, his wounded body and tired soul.</p>
    <p>After what seemed like hours, Menshin&#8217;s eyes opened at slits and 
      looked upwards at the sky. The sun was high in the sky and shined down almost 
      directly over his head.</p>
    <p>&#8220;I am surprised we found him myself,&#8221; Jurrah&#8217;s voice 
      whispered. Menshin&#8217;s ears picked up the whispering and looked over 
      his shoulder of the two riders chatting. Jurrah was off to the right and 
      the lead servant was at the left.</p>
    <p>&#8220;Well, he is very lucky we did. Did you see his right leg?&#8221;</p>
    <p>&#8220;Wait, which one? Both seemed tattered.&#8221;</p>
    <p>&#8220;The right one, the bloody one. You could tell it suffered much damage. 
      It is a wonder how he could stand on it so long before collapsing on the 
      ground.&#8221;</p>
    <p>&#8220;Oh, yes. The leg still covered in the armor plate?&#8221;</p>
    <p>&#8220;Yes, exactly. I could see blood dripping from it and a huge gash 
      right through the plate. Whatever hit him had great force and probably knocked 
      him down, which would probably explain the concussion.&#8221;</p>
    <p>&#8220;Ah, I see. You are most intuitive, friend. I guess that&#8217;s 
      why the king sent you out on this mission.&#8221;</p>
    <p>The other man chuckled and looked over at Jurrah, &#8220;It doesn&#8217;t 
      take much to do the things I do. In time you will become just as intuitive.&#8221;</p>
    <p>The men stopped talking and Menshin returned his gaze to the back on the 
      carriage. &#8220;My leg?&#8221; he thought, &#8220;never once did I,&#8221; 
      he stopped as his stare came to the shin of his right leg. A large gash 
      had eaten through the metal of his armor and dug right into his skin, possibly 
      even into his bone, however he could not gaze too fondly at the wound at 
      his angle. He could feel nausea seep through the recesses of his abdomen, 
      soon the feeling overwhelmed his head and he passed out in carriage.</p>
